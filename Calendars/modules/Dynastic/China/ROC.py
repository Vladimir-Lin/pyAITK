class ROCParser     ( Parser                                          ) :
  ############################################################################
  def __init__           ( self                                            ) :
    ##########################################################################
    super ( ) . __init__ (                                                   )
    ##########################################################################
    return
  ############################################################################
  def Keywords ( self                                                      ) :
    return     [ "%(R)"                                                    , \
                 "%(M)"                                                    , \
                 "%(D)"                                                    , \
                 "%(h)"                                                    , \
                 "%(m)"                                                    , \
                 "%(s)"                                                    , \
                 "%(TZ)"                                                     ]
  ############################################################################
  def Decoder                       ( self                                 , \
                                      Calendar                             , \
                                      inputString                          , \
                                      Format                               ) :
    ##########################################################################
    K        = self . KeywordExists ( Format , self . Keywords ( ) , 0       )
    ##########################################################################
    if                              ( len ( K ) <= 0                       ) :
      return False
    ##########################################################################
    R        = self . Dissects      ( Format , self . Keywords ( )           )
    ##########################################################################
    if                              ( len ( R ) <= 0                       ) :
      return False
    ##########################################################################
    J        = self . Parsing       ( inputString , K , R                    )
    S        = J    . keys          (                                        )
    ##########################################################################
    if                              ( len ( S ) <= 0                       ) :
      return False
    ##########################################################################
    for KEY in S                                                             :
      ########################################################################
      V            = J              [ KEY                                    ]
      ########################################################################
      if                            ( "%(R)" == KEY                        ) :
        ######################################################################
        try                                                                  :
          ####################################################################
          X        = int            ( V                                      )
          Calendar . YEAR = X
          ####################################################################
        except                                                               :
          pass
        ######################################################################
      elif                          ( "%(M)" == KEY                        ) :
        ######################################################################
        try                                                                  :
          ####################################################################
          X        = int            ( V                                      )
          ####################################################################
          if                        ( X < 1                                ) :
            ##################################################################
            X      = 1
            ##################################################################
          elif                      ( X > 12                               ) :
            ##################################################################
            X      = 12
          ####################################################################
          Calendar . MONTH = X
          ####################################################################
        except                                                               :
          pass
        ######################################################################
      elif                          ( "%(D)" == KEY                        ) :
        ######################################################################
        try                                                                  :
          ####################################################################
          X        = int            ( V                                      )
          ####################################################################
          if                        ( X < 1                                ) :
            ##################################################################
            X      = 1
            ##################################################################
          elif                      ( X > 31                               ) :
            ##################################################################
            X      = 31
          ####################################################################
          Calendar . DAY = X
          ####################################################################
        except                                                               :
          pass
        ######################################################################
      elif                          ( "%(h)" == KEY                        ) :
        ######################################################################
        try                                                                  :
          ####################################################################
          X        = int            ( V                                      )
          ####################################################################
          if                        ( X < 0                                ) :
            ##################################################################
            X      = 0
            ##################################################################
          elif                      ( X > 23                               ) :
            ##################################################################
            X      = 23
          ####################################################################
          Calendar . HOUR = X
          ####################################################################
        except                                                               :
          pass
        ######################################################################
      elif                          ( "%(m)" == KEY                        ) :
        ######################################################################
        try                                                                  :
          ####################################################################
          X        = int            ( V                                      )
          ####################################################################
          if                        ( X < 0                                ) :
            ##################################################################
            X      = 0
            ##################################################################
          elif                      ( X > 59                               ) :
            ##################################################################
            X      = 59
          ####################################################################
          Calendar . MINUTE = X
          ####################################################################
        except                                                               :
          pass
        ######################################################################
      elif                          ( "%(s)" == KEY                        ) :
        ######################################################################
        try                                                                  :
          ####################################################################
          X        = int            ( V                                      )
          ####################################################################
          if                        ( X < 0                                ) :
            ##################################################################
            X      = 0
            ##################################################################
          elif                      ( X > 59                               ) :
            ##################################################################
            X      = 59
          ####################################################################
          Calendar . SECOND = X
          ####################################################################
        except                                                               :
          pass
        ######################################################################
      elif                          ( "%(TZ)" == KEY                       ) :
        ######################################################################
        Calendar . setTimeZone      ( f"{V}"                                 )
    ##########################################################################
    Calendar     . localToStardate  (                                        )
    Calendar     . WEEKDAY = Calendar . toWeekOfDay ( Calendar . Stardate    )
    ##########################################################################
    return True
