class JulianDayFormatter ( Formatter                                       ) :
  ############################################################################
  def __init__           ( self                                            ) :
    ##########################################################################
    super ( ) . __init__ (                                                   )
    ##########################################################################
    return
  ############################################################################
  def Keywords ( self                                                      ) :
    return     [ "%J" , "%R" , "%D" , "%H" , "%M" , "%S"                     ]
  ############################################################################
  def toString                ( self , Calendar , Format                   ) :
    ##########################################################################
    V   = Calendar . Value
    R   = Calendar . Remain
    N   = Calendar . Norm
    S   = Format
    ##########################################################################
    if                        ( "%J" in S                                  ) :
      ########################################################################
      S = S     . replace     ( "%J" , f"{V}"                                )
    ##########################################################################
    if                        ( "%R" in S                                  ) :
      ########################################################################
      S = S     . replace     ( "%R" , f"{R}"                                )
    ##########################################################################
    S   = self  . toRemainDay ( S , N                                        )
    ##########################################################################
    return self . toHMS       ( S , R                                        )
