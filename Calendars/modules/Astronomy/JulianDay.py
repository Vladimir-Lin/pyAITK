class JulianDayParser    ( Parser                                          ) :
  ############################################################################
  def __init__           ( self                                            ) :
    ##########################################################################
    super ( ) . __init__ (                                                   )
    ##########################################################################
    return
  ############################################################################
  def Keywords ( self                                                      ) :
    return     [ "%J" , "%R" , "%H" , "%M" , "%S"                            ]
  ############################################################################
  def Decoder                       ( self                                 , \
                                      Calendar                             , \
                                      inputString                          , \
                                      Format                               ) :
    ##########################################################################
    K        = self . KeywordExists ( Format , self . Keywords ( ) , 0       )
    ##########################################################################
    if                              ( len ( K ) <= 0                       ) :
      return False
    ##########################################################################
    R        = self . Dissects      ( Format , self . Keywords ( )           )
    ##########################################################################
    if                              ( len ( R ) <= 0                       ) :
      return False
    ##########################################################################
    J        = self . Parsing       ( inputString , K , R                    )
    S        = J    . keys          (                                        )
    ##########################################################################
    if                              ( len ( S ) <= 0                       ) :
      return False
    ##########################################################################
    XJD      = Calendar . Value
    REMAIN   = Calendar . Remain
    HOURS    = self     . toHours   ( REMAIN                                 )
    MINS     = self     . toMinutes ( REMAIN                                 )
    SECS     = self     . toSeconds ( REMAIN                                 )
    ##########################################################################
    for KEY in S                                                             :
      ########################################################################
      V            = J              [ KEY                                    ]
      ########################################################################
      if                            ( "%R" == KEY                          ) :
        ######################################################################
        try                                                                  :
          REMAIN   = int            ( V                                      )
        except                                                               :
          return False
        ######################################################################
        HOURS      = self . toHours   ( REMAIN                               )
        MINS       = self . toMinutes ( REMAIN                               )
        SECS       = self . toSeconds ( REMAIN                               )
      ########################################################################
      elif                          ( "%J" == KEY                          ) :
        ######################################################################
        if                          ( "." in V                             ) :
          ####################################################################
          L        = V . split      ( "."                                    )
          if                        ( len ( L ) != 2                       ) :
            return False
          ####################################################################
          try                                                                :
            XJD    = int            ( L [ 0 ]                                )
          except                                                             :
            return False
          ####################################################################
          F        = L              [ 1                                      ]
          F        = f"0.{F}"
          ####################################################################
          try                                                                :
            REMAIN = int            ( float ( F ) * 86400                    )
          except                                                             :
            return False
          ####################################################################
          HOURS    = self . toHours   ( REMAIN                               )
          MINS     = self . toMinutes ( REMAIN                               )
          SECS     = self . toSeconds ( REMAIN                               )
          ####################################################################
        else                                                                 :
          ####################################################################
          try                                                                :
            XJD    = int            ( V                                      )
          except                                                             :
            return False
      ########################################################################
      elif                          ( "%H" == KEY                          ) :
        ######################################################################
        try                                                                  :
          HOURS    = int            ( V                                      )
        except                                                               :
          return False
      ########################################################################
      elif                          ( "%M" == KEY                          ) :
        ######################################################################
        try                                                                  :
          MINS     = int            ( V                                      )
        except                                                               :
          return False
      ########################################################################
      elif                          ( "%S" == KEY                          ) :
        ######################################################################
        try                                                                  :
          SECS     = int            ( V                                      )
        except                                                               :
          return False
    ##########################################################################
    REMAIN         =          int   ( HOURS * 3600                           )
    REMAIN         = REMAIN + int   ( MINS  *  60                            )
    REMAIN         = REMAIN + int   ( SECS                                   )
    ##########################################################################
    Calendar       . setXJD         ( XJD , REMAIN                           )
    ##########################################################################
    return True
