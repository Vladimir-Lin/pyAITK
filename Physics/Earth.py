    A     = gmpy2 . sin ( RLAT ) * gmpy2 . sin ( RDEC )
    B     = gmpy2 . cos ( RLAT ) * gmpy2 . cos ( RDEC )
    C     = gmpy2 . cos ( RZEN ) - A
    ##########################################################################
    HourAngle = gmpy2 . acos ( C / B                                         )
    ##########################################################################
    if                       ( not isRising                                ) :
      HourAngle = - HourAngle
    ##########################################################################
    return HourAngle
