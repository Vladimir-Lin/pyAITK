    ##########################################################################
    global SunEarthPerihelions
    ##########################################################################
    AT     = 0
    NEXT   = 1
    CNT    = len                 ( SunEarthPerihelions                       )
    ##########################################################################
    if                           ( SDTime < SunEarthPerihelions [ 0 ]      ) :
      return -1
    ##########################################################################
    while                        ( NEXT < CNT                              ) :
      ########################################################################
      V    = SunEarthPerihelions [ AT                                        ]
      N    = SunEarthPerihelions [ NEXT                                      ]
      ########################################################################
      if                         ( ( SDTime >= V ) and ( SDTime < N )      ) :
        return AT
      ########################################################################
      AT   = AT   + 1
      NEXT = NEXT + 1
    ##########################################################################
    return -1
