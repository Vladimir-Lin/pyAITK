  def ConnectDB                ( self , UsePure = False                    ) :
    ##########################################################################
    OK = self . DB . ConnectTo ( self . DbConf , UsePure                     )
    ##########################################################################
    if                         ( not OK                                    ) :
      return False
    ##########################################################################
    self . DB . Prepare        (                                             )
    ##########################################################################
    return   True
  ############################################################################
  def CloseDB              ( self                                          ) :
    ##########################################################################
    self . DB . Close      (                                                 )
    self . DB = Connection (                                                 )
    ##########################################################################
