    self . ActionListings    = [                                             ]
    self . ACTIONs           = {                                             }
    ##########################################################################
    self . ConditionItems    = [                                             ]
    self . ConditionMaps     = {                                             }
    self . ConditionUuids    = [                                             ]
    self . ActionItems       = [                                             ]
    self . ActionMaps        = {                                             }
    self . ActionUuids       = [                                             ]
    ##########################################################################
    self . Settings          = {                                             }
    self . Tables            = {                                             }
    self . DbConf            = {                                             }
    ##########################################################################
    self . Handler           = None
    self . SendMessage       = None
    self . LOG               = None
    self . DbCheck           = 0
    self . Ready             = False
    ##########################################################################
    self . DB                = Connection       (                            )
    self . DbLocker          = threading . Lock (                            )
    self . QueryLocker       = threading . Lock (                            )
