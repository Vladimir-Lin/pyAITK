  ############################################################################
  def AssureDB                     ( self                                  ) :
    ##########################################################################
    NOW  = StarDate                (                                         )
    NOW  . Now                     (                                         )
    CDT  = int                     ( NOW . Stardate                          )
    ZDT  = int                     ( CDT - self . DbCheck                    )
    ##########################################################################
    if                             ( ZDT < 60                              ) :
      return True
    ##########################################################################
    self . DbCheck = CDT
    ##########################################################################
    OK   = self . DB . isConnected (                                         )
    ##########################################################################
    if                             ( OK                                    ) :
      return True
    ##########################################################################
    return self . ConnectDB        (                                         )
  ############################################################################
  def Prepare                            ( self                            ) :
    ##########################################################################
    OK         = self . DB . isConnected (                                   )
    ##########################################################################
    if                                   ( not OK                          ) :
      return
    ##########################################################################
    ACTTAB     = "`cios`.`decisionactions`"
    CNDTAB     = "`cios`.`decisionconditions`"
    GRPTAB     = "`cios`.`decisionconditiongroups`"
    MAPTAB     = "`cios`.`decisionmappings`"
    ##########################################################################
    NOW        = StarDate                (                                   )
    NOW        . Now                     (                                   )
    CDT        = NOW . Stardate
    ##########################################################################
    self . DbLocker . acquire            (                                   )
    ##########################################################################
    ACTI       = ActionItem              (                                   )
    COND       = ConditionItem           (                                   )
    MAPI       = MappingItem             (                                   )
    ##########################################################################
    CITEMs     = COND . ObtainsAll       ( DB , CNDTAB , MAPTAB              )
    MITEMs     = COND . ToMappers        ( CITEMs                            )
    AITEMs     = ACTI . ObtainsAll       ( DB                              , \
                                           ACTTAB                          , \
                                           MAPTAB                          , \
                                           GRPTAB                          , \
                                           MITEMs                            )
    ##########################################################################
    self . DbLocker . release            (                                   )
    ##########################################################################
    CMaps                 = COND . ToMappers ( CITEMs                        )
    AMaps                 = ACTI . ToMappers ( AITEMs                        )
    ##########################################################################
    for C in CITEMs                                                          :
      ########################################################################
      if                                 ( C . Uuid in CMaps               ) :
        ######################################################################
        C . FromHistory                  ( CMaps [ C . Uuid                ] )
    ##########################################################################
    for A in AITEMs                                                          :
      ########################################################################
      if                                 ( A . Uuid in AMaps               ) :
        ######################################################################
        A . FromHistory                  ( AMaps [ A . Uuid                ] )
    ##########################################################################
    self  . QueryLocker    . acquire     (                                   )
    ##########################################################################
    self  . ConditionItems = CITEMs
    self  . ActionItems    = AITEMs
    ##########################################################################
    self  . ConditionUuids = COND . AllToUuids ( CITEMs                      )
    self  . ActionUuids    = ACTI . AllToUuids ( AITEMs                      )
    ##########################################################################
    self  . ConditionMaps  = COND . ToMappers  ( CITEMs                      )
    self  . ActionMaps     = ACTI . ToMappers  ( AITEMs                      )
    ##########################################################################
    self  . QueryLocker    . release     (                                   )
    ##########################################################################
    self  . Ready          = True
    ##########################################################################
    CNT   = len                          ( self . ConditionUuids             )
    ANT   = len                          ( self . ActionUuids                )
    ##########################################################################
    MSG   = f"Decision Center - {CNT} conditions, {ANT} actions loaded"
    self  . LOG                          ( MSG                               )
    ##########################################################################
    return
  ############################################################################
  def Sync                          ( self , CONDs , VALs                  ) :
    ##########################################################################
    if                              ( len ( CONDs ) <= 0                   ) :
      return
    ##########################################################################
    OK    = self . DB . isConnected (                                        )
    ##########################################################################
    if                              ( not OK                               ) :
      return
    ##########################################################################
    self  . DbLocker . acquire      (                                        )
    ##########################################################################
    for CUID in CONDs                                                        :
      ########################################################################
      VAL = VALs                    [ CUID                                   ]
      ########################################################################
      QQ  = f"""update `cios`.`decisionconditions`
                set `states` = {VAL}
                where ( `uuid` = {CUID} ) ;"""
      QQ  = QQ  . replace           ( "\r" , " "                             )
      QQ  = QQ  . replace           ( "\n" , " "                             )
      QQ  = " " . join              ( QQ . split (                         ) )
      ########################################################################
      self . DB . Query             ( QQ                                     )
    ##########################################################################
    self  . DbLocker . release      (                                        )
    ##########################################################################
    return
  ############################################################################
  def Flush                       ( self                                   ) :
    ##########################################################################
    CONDs =                       [                                          ]
    VALs  =                       {                                          }
    ##########################################################################
    self  . QueryLocker . acquire (                                          )
    ##########################################################################
    for CUID in self . ConditionListings                                     :
      ########################################################################
      CT  = self . CONDITIONs     [ CUID ] [ "type"                          ]
      SY  = self . CONDITIONs     [ CUID ] [ "sync"                          ]
      ST  = self . CONDITIONs     [ CUID ] [ "states"                        ]
      ########################################################################
      if                          ( SY and ( CT in [ 2 , 3 ] )             ) :
        ######################################################################
        CONDs . append            ( CUID                                     )
        VALs [ CUID ] = ST
    ##########################################################################
    self  . QueryLocker . release (                                          )
    ##########################################################################
    self  . Sync                  ( CONDs , VALs                             )
    ##########################################################################
    return
  ############################################################################
  def FlushCaching                ( self                                   ) :
    ##########################################################################
    CONDs =                       [                                          ]
    VALs  =                       {                                          }
    ##########################################################################
    NOW   = StarDate              (                                          )
    NOW   . Now                   (                                          )
    CDT   = NOW . Stardate
    ##########################################################################
    self  . QueryLocker . acquire (                                          )
    ##########################################################################
    for CUID in self . ConditionListings                                     :
      ########################################################################
      UD  = self . CONDITIONs     [ CUID ] [ "update"                        ]
      CT  = self . CONDITIONs     [ CUID ] [ "type"                          ]
      SY  = self . CONDITIONs     [ CUID ] [ "sync"                          ]
      ST  = self . CONDITIONs     [ CUID ] [ "states"                        ]
      ########################################################################
      if                          ( SY and ( CT in [ 2 , 3 ] )             ) :
        ######################################################################
        D = int                   ( CDT - UD                                 )
        ######################################################################
        if                        ( D >= 5                                 ) :
          ####################################################################
          CONDs . append          ( CUID                                     )
          VALs [ CUID ] = ST
    ##########################################################################
    self  . QueryLocker . release (                                          )
    ##########################################################################
    self  . Sync                  ( CONDs , VALs                             )
    ##########################################################################
    return
  ############################################################################
  def DoTasks                     ( self                                   ) :
    ##########################################################################
    CONDs =                       [                                          ]
    ACTs  =                       [                                          ]
    ACTz  =                       {                                          }
    ##########################################################################
    NOW   = StarDate              (                                          )
    NOW   . Now                   (                                          )
    CDT   = NOW . Stardate
    ##########################################################################
    self  . QueryLocker . acquire (                                          )
    ##########################################################################
    for CUID in self . ConditionListings                                     :
      ########################################################################
      PD  = self . CONDITIONs     [ CUID ] [ "pending"                       ]
      ########################################################################
      if                          ( PD                                     ) :
        ######################################################################
        CONDs . append            ( CUID                                     )
        ACTX  = self . CONDITIONs [ CUID ] [ "actions"                       ]
        ######################################################################
        for ACT in ACTX                                                      :
          ####################################################################
          if                      ( ACT not in ACTs                        ) :
            ##################################################################
            ACTs . append         ( ACT                                      )
    ##########################################################################
    for AUID in ACTs                                                         :
      ########################################################################
      CJ =                        {                                          }
      ########################################################################
      for CUID in self . ACTIONs [ AUID ] [ "conditions" ]                   :
        ######################################################################
        CJ [ CUID ] = self . CONDITIONs [ CUID                               ]
      ########################################################################
      ACTz [ AUID ] = CJ
    ##########################################################################
    for CUID in CONDs                                                        :
      ########################################################################
      PD  = self . CONDITIONs     [ CUID ] [ "pending"                       ]
      ########################################################################
      if                          ( PD                                     ) :
        ######################################################################
        self . CONDITIONs [ CUID ] [ "pending" ] = False
    ##########################################################################
    self  . QueryLocker . release (                                          )
    ##########################################################################
    if                            ( self . Handler in [ False , None     ] ) :
      return
    ##########################################################################
    if                            ( len ( ACTs ) <= 0                      ) :
      return
    ##########################################################################
    for ACT in ACTs                                                          :
      ########################################################################
      self . Handler              ( ACT , ACTz [ AUID                      ] )
    ##########################################################################
    return
  ############################################################################
  def Reload       ( self                                                  ) :
    ##########################################################################
    self . Flush   (                                                         )
    self . Prepare (                                                         )
    ##########################################################################
    return
  ############################################################################
  def Process           ( self                                             ) :
    ##########################################################################
    self . DoTasks      (                                                    )
    self . FlushCaching (                                                    )
    ##########################################################################
    return
  ############################################################################
  def ConditionJson               ( self , CUID                            ) :
    ##########################################################################
    if                            ( CUID not in self . ConditionListings   ) :
      return                      { "Condition" : CUID                     , \
                                    "Exists"    : False                    , \
                                    "State"     : -1                         }
    ##########################################################################
    self  . QueryLocker . acquire (                                          )
    State = self . CONDITIONs     [ CUID ] [ "states"                        ]
    self  . QueryLocker . release (                                          )
    ##########################################################################
    return                        { "Condition" : CUID                     , \
                                    "Exists"    : True                     , \
                                    "State"     : State                      }
  ############################################################################
  def UpdateCondition              ( self , CUID , State                   ) :
    ##########################################################################
    if                             ( not self . Ready                      ) :
      return
    ##########################################################################
    if                             ( CUID not in self . ConditionListings  ) :
      return
    ##########################################################################
    NOW    = StarDate              (                                         )
    NOW    . Now                   (                                         )
    CDT    = NOW . Stardate
    ##########################################################################
    self   . QueryLocker . acquire (                                         )
    ##########################################################################
    T      = self . CONDITIONs     [ CUID ] [ "type"                         ]
    ##########################################################################
    if                             ( T in [ 2 , 3                        ] ) :
      ########################################################################
      self . CONDITIONs [ CUID ] [ "sync"    ] = True
    ##########################################################################
    self   . CONDITIONs [ CUID ] [ "pending" ] = True
    self   . CONDITIONs [ CUID ] [ "update"  ] = CDT
    self   . CONDITIONs [ CUID ] [ "states"  ] = State
    ##########################################################################
    self   . QueryLocker . release (                                         )
    ##########################################################################
    return
  ############################################################################
  def RpcQueryCondition ( self , J , ADDR                                  ) :
    ##########################################################################
    ANSWER          =   { "Answer" : "Yes" , "IP"       : ADDR               }
    RESP            =   { "Answer" : 202   , "Response" : ANSWER             }
    ##########################################################################
    if                  ( "Condition" in J                                 ) :
      ########################################################################
      CUID          = J [ "Condition"                                        ]
      JSOX          = self . ConditionJson ( CUID                            )
      JSOX [ "IP" ] = ADDR
      ########################################################################
      return            { "Answer" : 202   , "Response" : JSOX               }
    ##########################################################################
    return RESP
  ############################################################################
  def RpcQuery        ( self , J , ADDR                                    ) :
    ##########################################################################
    ANSWER        =   { "Answer" : "Yes" , "IP"       : ADDR                 }
    RESP          =   { "Answer" : 202   , "Response" : ANSWER               }
    ##########################################################################
    if                ( "Condition" in J                                   ) :
      ########################################################################
      CUID        = J [ "Condition"                                          ]
      ########################################################################
    else                                                                     :
      ########################################################################
      return RESP
    ##########################################################################
    if                ( "State"     in J                                   ) :
      ########################################################################
      STAT        = J [ "State"                                              ]
      ########################################################################
    else                                                                     :
      ########################################################################
      return RESP
    ##########################################################################
    self          . UpdateCondition      ( CUID , STAT                       )
    JSOX          = self . ConditionJson ( CUID                              )
    JSOX [ "IP" ] = ADDR
    ##########################################################################
    return            { "Answer" : 202   , "Response" : JSOX                 }
  ############################################################################
  def RpcConditions ( self , J , ADDR                                      ) :
    ##########################################################################
    ANSWER    =     { "Answer" : "Yes" , "IP"       : ADDR                   }
    RESP      =     { "Answer" : 202   , "Response" : ANSWER                 }
    ##########################################################################
    if              ( "Conditions" not in J                                ) :
      ########################################################################
      return RESP
    ##########################################################################
    JSOX      =     { "Answer" : "Yes" , "IP"       : ADDR                   }
    ##########################################################################
    ##########################################################################
    ##########################################################################
    JSOX [ "IP" ] = ADDR
    ##########################################################################
    return          { "Answer" : 202   , "Response" : JSOX                   }
  ############################################################################
  def RpcReload        ( self , J , ADDR                                   ) :
    ##########################################################################
    ANSWER    =        { "Answer" : "Yes" , "IP"       : ADDR                }
    RESP      =        { "Answer" : 202   , "Response" : ANSWER              }
    ##########################################################################
    threading . Thread ( target = self . Reload                            ) \
              . start  (                                                     )
    ##########################################################################
    return RESP
  ############################################################################
  def RpcHandler ( self , J , ADDR                                         ) :
    ##########################################################################
    ANSWER =     { "Answer" : "Yes" , "IP"       : ADDR                      }
    RESP   =     { "Answer" : 202   , "Response" : ANSWER                    }
    ##########################################################################
    if           ( "Action" not in J                                       ) :
      return RESP
    ##########################################################################
    ACTION = J   [ "Action"                                                  ]
    ##########################################################################
    if           ( "Query" == ACTION                                       ) :
      ########################################################################
      return self . RpcQueryCondition ( J , ADDR                             )
    ##########################################################################
    if           ( "Update" == ACTION                                      ) :
      ########################################################################
      return self . RpcQuery          ( J , ADDR                             )
    ##########################################################################
    if           ( "Conditions" == ACTION                                  ) :
      ########################################################################
      return self . RpcConditions     ( J , ADDR                             )
    ##########################################################################
    if           ( "Reload" == ACTION                                      ) :
      ########################################################################
      return self . RpcReload         ( J , ADDR                             )
    ##########################################################################
    return RESP
