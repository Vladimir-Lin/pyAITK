  def set             ( self , item , value                                ) :
    ##########################################################################
    a = item . lower  (                                                      )
    ##########################################################################
    if                ( "id"      == a                                     ) :
      ########################################################################
      self . Id             = int   ( value                                  )
    ##########################################################################
    elif              ( "uuid"    == a                                     ) :
      ########################################################################
      self . Uuid           = int   ( value                                  )
    ##########################################################################
    elif              ( "used"    == a                                     ) :
      ########################################################################
      self . Used           = int   ( value                                  )
    ##########################################################################
    elif              ( "type"    == a                                     ) :
      ########################################################################
      self . ConditionType  = int   ( value                                  )
    ##########################################################################
    elif              ( "form"    == a                                     ) :
      ########################################################################
      self . Form           = int   ( value                                  )
      ########################################################################
      self . SyncForm (                                                      )
    ##########################################################################
    elif              ( "states"  == a                                     ) :
      ########################################################################
      self . PreviousStates = int   ( value                                  )
      self . States         = int   ( value                                  )
    ##########################################################################
    elif              ( "value"   == a                                     ) :
      ########################################################################
      self . PreviousValue  = float ( value                                  )
      self . Value          = float ( value                                  )
    ##########################################################################
    elif              ( "name"    == a                                     ) :
      ########################################################################
      self . Name           = value . decode ( "utf-8"                       )
    ##########################################################################
    elif              ( "json"    == a                                     ) :
      ########################################################################
      try                                                                    :
        ######################################################################
        S                   = value . decode ( "utf-8"                       )
        self . Properties   = json  . loads  ( S                             )
        ######################################################################
      except                                                                 :
        ######################################################################
        self . Properties   = {                                              }
    ##########################################################################
    elif              ( "actions" == a                                     ) :
      self . Actions        = value
    ##########################################################################
    elif              ( "ltime"   == a                                     ) :
      self . ltime          = value
    ##########################################################################
    return
