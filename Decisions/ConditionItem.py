  def SyncForm ( self                                                      ) :
    ##########################################################################
    if         ( 0 == self . Form                                          ) :
      ########################################################################
      self . valueChange = self . setStates
      self . SyncSQL     = self . syncStates
      self . isChanged   = self . intChanged
      ########################################################################
    elif       ( 1 == self . Form                                          ) :
      ########################################################################
      self . valueChange = self . setValue
      self . SyncSQL     = self . syncValue
      self . isChanged   = self . doubleChanged
    ##########################################################################
    return
