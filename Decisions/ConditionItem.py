  def syncStates      ( self , DB , TABLE                                  ) :
    ##########################################################################
    UUID = self . Uuid
    STAT = self . States
    ##########################################################################
    QQ   = f"""update {TABLE}
               set `states` = {STAT}
               where ( `uuid` = {UUID} ) ;"""
    QQ   = " " . join ( QQ . split (                                       ) )
    DB   . Query      ( QQ                                                   )
    ##########################################################################
    self . Updated = False
    ##########################################################################
    return
  ############################################################################
  def syncValue        ( self , DB , TABLE                                 ) :
    ##########################################################################
    UUID = self . Uuid
    ##########################################################################
    QQ   = f"""update {TABLE}
               set `value` = ?
               where ( `uuid` = {UUID} ) ;"""
    QQ   = " " . join  ( QQ . split (                                      ) )
    DB   . QueryValues ( QQ , ( self . Value , )                             )
    ##########################################################################
    self . Updated = False
    ##########################################################################
    return
  ############################################################################
  def intChanged ( self                                                    ) :
    return       ( self . PreviousStates != self . States                    )
  ############################################################################
  def doubleChanged ( self                                                 ) :
    ##########################################################################
    D = float       ( self . PreviousValue - self . Value                    )
    ##########################################################################
    if              ( D < 0.0                                              ) :
      ########################################################################
      D = -D
    ##########################################################################
    return          ( D > self . Difference                                  )
  ############################################################################
  def isUpdated ( self                                                     ) :
    return self . Updated
