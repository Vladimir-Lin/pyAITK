import requests
import json
import urllib
##############################################################################
## 語言翻譯
##############################################################################
def Translate                   ( message , fromLC , toLC                  ) :
  ############################################################################
  HREF = "https://translate.googleapis.com/translate_a/single"
  EMSG = urllib . parse . quote ( message                                    )
  STLG = f"sl={fromLC}&tl={toLC}"
  QTXT = f"q={EMSG}"
  URX  = f"{HREF}?client=gtx&{STLG}&dt=t&dt=t&{QTXT}"
  ############################################################################
  try                                                                        :
    ##########################################################################
    r  = requests . get         ( URX                                        )
    J  = json     . loads       ( json . dumps ( r . json (              ) ) )
    ##########################################################################
  except                                                                     :
    return ""
  ############################################################################
  C    = 0
  L    = J                      [ C                                          ]
  ############################################################################
  while                         ( C < len ( L )                            ) :
    ##########################################################################
    M  = L                      [ C                                          ]
    S  = M                      [ 0                                          ]
    ##########################################################################
    if                          ( S not in [ False , None ]                ) :
      return S
    ##########################################################################
    C  = C + 1
  ############################################################################
  return ""
##############################################################################
