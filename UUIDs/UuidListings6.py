class UuidListings                 ( TreeDock                              ) :
  ############################################################################
  HavingMenu = 1371434312
  ############################################################################
  emitLog    = Signal              ( str                                     )
  ############################################################################
  def __init__                     ( self , parent = None , plan = None    ) :
    ##########################################################################
    super ( ) . __init__           (        parent        , plan             )
    ##########################################################################
    self . dockingOrientation = Qt . Vertical
    self . dockingPlace       = Qt . RightDockWidgetArea
    self . dockingPlaces      = Qt . TopDockWidgetArea                     | \
                                Qt . BottomDockWidgetArea                  | \
                                Qt . LeftDockWidgetArea                    | \
                                Qt . RightDockWidgetArea
    ##########################################################################
    self . setColumnCount          ( 2                                       )
    self . setColumnHidden         ( 1 , True                                )
    self . setRootIsDecorated      ( False                                   )
    self . setAlternatingRowColors ( True                                    )
    ##########################################################################
    self . MountClicked            ( 1                                       )
    ##########################################################################
    self . assignSelectionMode     ( "ContiguousSelection"                   )
    ##########################################################################
    self . setFunction             ( self . FunctionDocking , True           )
    self . setFunction             ( self . HavingMenu      , True           )
    ##########################################################################
    self . setDragEnabled          ( False                                   )
    self . setDragDropMode         ( QAbstractItemView . DropOnly            )
    ##########################################################################
    return
  ############################################################################
  def AttachActions   ( self         ,                      Enabled        ) :
    ##########################################################################
    self . LinkAction ( "Refresh"    , self . startup     , Enabled          )
    ##########################################################################
    self . LinkAction ( "Insert"     , self . InsertItem  , Enabled          )
    self . LinkAction ( "Delete"     , self . DeleteItems , Enabled          )
    ##########################################################################
    self . LinkAction ( "Paste"      , self . PasteItems  , Enabled          )
    ##########################################################################
    self . LinkAction ( "Select"     , self . SelectOne   , Enabled          )
    self . LinkAction ( "SelectAll"  , self . SelectAll   , Enabled          )
    self . LinkAction ( "SelectNone" , self . SelectNone  , Enabled          )
    ##########################################################################
    M    = self . windowTitle (                                              )
    ##########################################################################
    if                        ( Enabled                                    ) :
      ########################################################################
      M  = f"Enter {M}"
      ########################################################################
    else                                                                     :
      ########################################################################
      M  = f"Leave {M}"
    ##########################################################################
    self . emitLog . emit     ( M                                            )
    ##########################################################################
    return
  ############################################################################
  def FocusIn             ( self                                           ) :
    ##########################################################################
    if                    ( not self . isPrepared ( )                      ) :
      return False
    ##########################################################################
    self . setActionLabel ( "Label" , self . windowTitle ( )                 )
    self . AttachActions  ( True                                             )
    ##########################################################################
    return True
  ############################################################################
  def closeEvent             ( self , event                                ) :
    ##########################################################################
    self . AttachActions     ( False                                         )
    self . defaultCloseEvent (        event                                  )
    ##########################################################################
    return
  ############################################################################
  def startup                ( self                                        ) :
    ##########################################################################
    global SystemUUIDs
    ##########################################################################
    self   . clear           (                                               )
    ##########################################################################
    for UUID in SystemUUIDs                                                  :
      ########################################################################
      item = QTreeWidgetItem (                                               )
      item . setText         ( 0 , str ( UUID )                              )
      item . setData         ( 0 , Qt . UserRole , UUID                      )
      self . addTopLevelItem ( item                                          )
    ##########################################################################
    return
  ############################################################################
  def singleClicked             ( self , item , column                     ) :
    ##########################################################################
    self . defaultSingleClicked (        item , column                       )
    ##########################################################################
    return
  ############################################################################
  def Prepare             ( self                                           ) :
    ##########################################################################
    self . defaultPrepare ( "UuidListings" , 1                               )
    ##########################################################################
    return
  ############################################################################
  def InsertItem              ( self                                       ) :
    ##########################################################################
    item = QTreeWidgetItem    (                                              )
    item . setData            ( 0 , Qt . UserRole , 0                        )
    self . addTopLevelItem    ( item                                         )
    line = self . setLineEdit ( item                                       , \
                                0                                          , \
                                "editingFinished"                          , \
                                self . nameChanged                           )
    line . setFocus           ( Qt . TabFocusReason                          )
    ##########################################################################
    return
  ############################################################################
  def nameChanged                 ( self                                   ) :
    ##########################################################################
    global SystemUUIDs
    ##########################################################################
    if                            ( not self . isItemPicked ( )            ) :
      return False
    ##########################################################################
    item   = self . CurrentItem   [ "Item"                                   ]
    column = self . CurrentItem   [ "Column"                                 ]
    line   = self . CurrentItem   [ "Widget"                                 ]
    text   = self . CurrentItem   [ "Text"                                   ]
    msg    = line . text          (                                          )
    uuid   = self . itemUuid      ( item , 0                                 )
    ##########################################################################
    if                            ( len ( msg ) <= 0                       ) :
      ########################################################################
      if                          ( uuid <= 0                              ) :
        self . removeTopLevelItem ( item                                     )
        return
    ##########################################################################
    uuid   = int                  ( msg                                      )
    item   . setText              ( column ,                 msg             )
    item   . setData              ( column , Qt . UserRole , uuid            )
    ##########################################################################
    self   . removeParked         (                                          )
    ##########################################################################
    SystemUUIDs . append          ( uuid                                     )
    ##########################################################################
    return
  ############################################################################
  def PasteItems                        ( self                             ) :
    ##########################################################################
    global SystemUUIDs
    ##########################################################################
    T     = qApp . clipboard ( ) . text (                                    )
    ##########################################################################
    if                                  ( len ( T ) <= 0                   ) :
      return
    ##########################################################################
    LINEs = T . split                   ( "\n"                               )
    if                                  ( len ( LINEs ) <= 0               ) :
      return
    ##########################################################################
    for L in LINEs                                                           :
      ########################################################################
      SystemUUIDs . append              ( int ( L )                          )
    ##########################################################################
    self . startup                      (                                    )
    ##########################################################################
    return
  ############################################################################
  def DeleteItems                ( self                                    ) :
    ##########################################################################
    global SystemUUIDs
    ##########################################################################
    items = self . selectedItems (                                           )
    if                           ( len ( items ) <= 0                      ) :
      return
    ##########################################################################
    for item in items                                                        :
      ########################################################################
      L   = item . text          ( 0                                         )
      U   = int                  ( L                                         )
      SystemUUIDs . remove       ( U                                         )
    ##########################################################################
    self . startup               (                                           )
    ##########################################################################
    return
  ############################################################################
  def Menu                         ( self , pos                            ) :
    ##########################################################################
    global SystemUUIDs
    ##########################################################################
    if                             ( not self . isPrepared ( )             ) :
      return False
    ##########################################################################
    doMenu = self . isFunction     ( self . HavingMenu                       )
    if                             ( not doMenu                            ) :
      return False
    ##########################################################################
    self   . Notify                ( 0                                       )
    ##########################################################################
    items , atItem , uuid = self . GetMenuDetails ( 0                        )
    ##########################################################################
    mm     = MenuManager           ( self                                    )
    ##########################################################################
    self   . AppendRefreshAction   ( mm , 1001                               )
    self   . AppendInsertAction    ( mm , 1101                               )
    ##########################################################################
    MSG    = self . getMenuItem    ( "Clear"                                 )
    mm     . addAction             ( 1201 , MSG                              )
    ##########################################################################
    if                             ( atItem not in [ False , None ]        ) :
      ########################################################################
      self . AppendDeleteAction    ( mm , 1202                               )
    ##########################################################################
    mm     . addSeparator          (                                         )
    self   . DockingMenu           ( mm                                      )
    ##########################################################################
    mm     . setFont               ( self    . menuFont ( )                  )
    aa     = mm . exec_            ( QCursor . pos      ( )                  )
    at     = mm . at               ( aa                                      )
    ##########################################################################
    if                             ( self . RunDocking   ( mm , aa )       ) :
      return True
    ##########################################################################
    if                             ( at == 1001                            ) :
      ########################################################################
      self . startup               (                                         )
      ########################################################################
      return True
    ##########################################################################
    if                             ( at == 1101                            ) :
      self . InsertItem            (                                         )
      return True
    ##########################################################################
    if                             ( at == 1201                            ) :
      ########################################################################
      SystemUUIDs =                [                                         ]
      ########################################################################
      self . startup               (                                         )
      ########################################################################
      return True
    ##########################################################################
    if                             ( at == 1202                            ) :
      ########################################################################
      uxid        = atItem . text  ( 0                                       )
      uuid        = int            ( uxid                                    )
      ########################################################################
      SystemUUIDs . remove         ( uuid                                    )
      ########################################################################
      self . startup               (                                         )
      ########################################################################
      return True
    ##########################################################################
    return True
