def appendUuids                 ( UUIDs                                    ) :
  ############################################################################
  global defaultUuidListings
  global SystemUUIDs
  ############################################################################
  for UUID in UUIDs                                                          :
    ##########################################################################
    SystemUUIDs       . append  ( UUID                                       )
  ############################################################################
  defaultUuidListings . startup (                                            )
  ############################################################################
  return
