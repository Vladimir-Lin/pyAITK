class ExecutionLogger     ( PlainTextEdit                                  ) :
  ############################################################################
  HavingMenu     = 1371434312
  ############################################################################
  emitAddText    = Signal ( str                                              )
  emitWindowIcon = Signal ( bool                                             )
  ############################################################################
  def __init__                       ( self , parent = None , plan = None  ) :
    ##########################################################################
    super ( ) . __init__             ( parent , plan                         )
    ##########################################################################
    self . dockingOrientation = Qt . Horizontal
    self . dockingPlace       = Qt . BottomDockWidgetArea
    self . dockingPlaces      = Qt . LeftDockWidgetArea                    | \
                                Qt . RightDockWidgetArea                   | \
                                Qt . TopDockWidgetArea                     | \
                                Qt . BottomDockWidgetArea
    ##########################################################################
    self . setFunction               ( self . FunctionDocking , True         )
    self . setFunction               ( self . HavingMenu      , True         )
    ##########################################################################
    self . emitAddText    . connect  ( self . appendPlainText                )
    self . emitWindowIcon . connect  ( self . assignWindowIcon               )
    ##########################################################################
    self . setReadOnly               ( True                                  )
    self . TZ          = "Asia/Taipei"
    self . receiveLog  = True
    self . AtMenu      = False
    self . defaultFont = self . font (                                       )
    ##########################################################################
    self . setPrepared               ( True                                  )
    ##########################################################################
    return
  ############################################################################
  def sizeHint                   ( self                                    ) :
    return self . SizeSuggestion ( QSize (  320 , 200 )                      )
  ############################################################################
  def DockLocationChanged      ( self , area                               ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  def Relocation               ( self                                      ) :
    ##########################################################################
    if                         ( not self . isPrepared ( )                 ) :
      return False
    ##########################################################################
    ##########################################################################
    return False
  ############################################################################
  def PrepareForActions             ( self                                 ) :
    ##########################################################################
    self . AppendSideActionWithIcon ( "DefaultFont"                        , \
                                      ":/images/font.png"                  , \
                                      self . AssignDefaultFont             , \
                                      True                                 , \
                                      False                                  )
    ##########################################################################
    return
  ############################################################################
  def AttachActions   ( self        ,                    Enabled           ) :
    ##########################################################################
    self . LinkAction ( "Delete"    , self . clear     , Enabled             )
    self . LinkAction ( "Copy"      , self . copy      , Enabled             )
    self . LinkAction ( "SelectAll" , self . selectAll , Enabled             )
    self . LinkAction ( "ZoomIn"    , self . ZoomIn    , Enabled             )
    self . LinkAction ( "ZoomOut"   , self . ZoomOut                         )
    ##########################################################################
    return
  ############################################################################
  def FocusIn                ( self                                        ) :
    ##########################################################################
    if                       ( not self . isPrepared ( )                   ) :
      return False
    ##########################################################################
    self . setActionLabel    ( "Label" , self . windowTitle ( )              )
    self . AttachActions     ( True                                          )
    self . attachActionsTool (                                               )
    ##########################################################################
    return True
  ############################################################################
  def FocusOut ( self                                                      ) :
    ##########################################################################
    if                         ( not self . isPrepared ( )                 ) :
      return True
    ##########################################################################
    if                         ( not self . AtMenu                         ) :
      ########################################################################
      self . AttachActions     ( False                                       )
      self . detachActionsTool (                                             )
    ##########################################################################
    return False
  ############################################################################
  def Shutdown               ( self                                        ) :
    ##########################################################################
    self . StayAlive   = False
    self . LoopRunning = False
    ##########################################################################
    if                       ( self . isThreadRunning (                  ) ) :
      return False
    ##########################################################################
    self . AttachActions     ( False                                         )
    ## self . detachActionsTool (                                               )
    self . LinkVoice         ( None                                          )
    ##########################################################################
    ## self . Leave . emit      ( self                                          )
    ##########################################################################
    return True
  ############################################################################
  def startup                        ( self                                ) :
    ##########################################################################
    self . defaultFont = self . font (                                       )
    self . show                      (                                       )
    ##########################################################################
    return
  ############################################################################
  def CurrentMoment              ( self , TZ = "Asia/Taipei"               ) :
    ##########################################################################
    NOW = StarDate               (                                           )
    NOW . Now                    (                                           )
    DTS = NOW . toDateTimeString ( TZ , " " , "%Y/%m/%d" , "%H:%M:%S"        )
    ##########################################################################
    return DTS
  ############################################################################
  def addText                   ( self , message                           ) :
    ##########################################################################
    if                          ( self . receiveLog                        ) :
      self . emitAddText . emit (        message                             )
    ##########################################################################
    return
  ############################################################################
  def assignWindowIcon     ( self , onOff                                  ) :
    ##########################################################################
    if                     ( onOff                                         ) :
      self . setWindowIcon ( self . onIcon                                   )
    else                                                                     :
      self . setWindowIcon ( self . offIcon                                  )
    ##########################################################################
    return
  ############################################################################
  def ZoomIn      ( self                                                   ) :
    ##########################################################################
    self . zoomIn ( 1                                                        )
    ##########################################################################
    return
  ############################################################################
  def ZoomOut      ( self                                                  ) :
    ##########################################################################
    self . zoomOut ( 1                                                       )
    ##########################################################################
    return
  ############################################################################
  def AssignDefaultFont ( self                                             ) :
    ##########################################################################
    self . setFont      ( self . defaultFont                                 )
    ##########################################################################
    return
  ############################################################################
  def TextingMenu           ( self , mm                                    ) :
    ##########################################################################
    TRX = self . Translations
    LOM = mm   . addMenu    (              TRX [ "UI::Texting"             ] )
    ##########################################################################
    ## mm  . addActionFromMenu ( LOM , 1001 , TRX [ "UI::ClearAll"            ] )
    mm  . addActionFromMenu ( LOM , 1002 , TRX [ "UI::CopyToClipboard"     ] )
    mm  . addActionFromMenu ( LOM , 1003 , TRX [ "UI::SelectAll"           ] )
    ##########################################################################
    return mm
  ############################################################################
  def RunTextingMenu   ( self , at                                         ) :
    ##########################################################################
    if                 ( at == 1001                                        ) :
      self . clear     (                                                     )
      return True
    ##########################################################################
    if                 ( at == 1002                                        ) :
      self . copy      (                                                     )
      return True
    ##########################################################################
    if                 ( at == 1003                                        ) :
      self . selectAll (                                                     )
      return True
    ##########################################################################
    return False
  ############################################################################
  def DisplayMenu                   ( self , mm                            ) :
    ##########################################################################
    TRX = self . Translations
    LOM = mm   . addMenu            ( TRX [ "UI::Display"                  ] )
    ICI = QIcon                     ( ":/images/zoomin.png"                  )
    ICO = QIcon                     ( ":/images/zoomout.png"                 )
    ##########################################################################
    mm  . addActionFromMenuWithIcon ( LOM , 1004 , ICI , TRX [ "UI::ZoomIn"  ] )
    mm  . addActionFromMenuWithIcon ( LOM , 1005 , ICO , TRX [ "UI::ZoomOut" ] )
    ##########################################################################
    return mm
  ############################################################################
  def RunDisplayMenu ( self , at                                           ) :
    ##########################################################################
    if               ( at == 1004                                          ) :
      self . ZoomIn  (                                                       )
      return True
    ##########################################################################
    if               ( at == 1005                                          ) :
      self . ZoomOut (                                                       )
      return True
    ##########################################################################
    return False
  ############################################################################
  def Menu                      ( self , pos                               ) :
    ##########################################################################
    doMenu = self . isFunction  ( self . HavingMenu                          )
    if                          ( not doMenu                               ) :
      return False
    ##########################################################################
    mm     = MenuManager        ( self                                       )
    ##########################################################################
    TRX    = self . Translations
    ##########################################################################
    mm     . addAction          ( 1101                                     , \
                                  TRX [ "UI::ReceiveLog" ]                 , \
                                  True                                     , \
                                  self . receiveLog                          )
    msg    = self . getMenuItem ( "DefaultFont"                              )
    icon   = QIcon              ( ":/images/font.png"                        )
    mm     . addActionWithIcon  ( 1102 , icon , msg                          )
    msg    = self . getMenuItem ( "ClearAll"                                 )
    icon   = QIcon              ( ":/images/undecided.png"                   )
    mm     . addActionWithIcon  ( 1103 , icon , msg                          )
    mm     . addSeparator       (                                            )
    ##########################################################################
    mm     = self . TextingMenu ( mm                                         )
    mm     = self . DisplayMenu ( mm                                         )
    ##########################################################################
    mm     . addSeparator       (                                            )
    ##########################################################################
    self   . DockingMenu        ( mm                                         )
    ##########################################################################
    self   . AtMenu = True
    ##########################################################################
    mm     . setFont            ( self    . font ( )                         )
    aa     = mm . exec_         ( QCursor . pos  ( )                         )
    at     = mm . at            ( aa                                         )
    ##########################################################################
    self   . AtMenu = False
    ##########################################################################
    if                          ( self . RunTextingMenu ( at      )        ) :
      return True
    ##########################################################################
    if                          ( self . RunDisplayMenu ( at      )        ) :
      return True
    ##########################################################################
    if                          ( self . RunDocking     ( mm , aa )        ) :
      return True
    ##########################################################################
    if                          ( 1101 == at                               ) :
      ########################################################################
      self . receiveLog = not self . receiveLog
      ########################################################################
      return True
    ##########################################################################
    if                          ( 1102 == at                               ) :
      ########################################################################
      self . AssignDefaultFont  (                                            )
      ########################################################################
      return True
    ##########################################################################
    if                          ( 1103 == at                               ) :
      ########################################################################
      self . clear              (                                            )
      ########################################################################
      return True
    ##########################################################################
    return True
