  def loading                        ( self                                ) :
    ##########################################################################
    DB       = self . ConnectDB      (                                       )
    if                               ( DB == None                          ) :
      self   . emitStartModel . emit (                                       )
      return
    ##########################################################################
    OKAY     = False
    self     . Notify                ( 3                                     )
    ##########################################################################
    FMT      = self . Translations   [ "UI::StartLoading"                    ]
    MSG      = FMT . format          ( self . windowTitle ( )                )
    self     . ShowStatus            ( MSG                                   )
    self     . OnBusy  . emit        (                                       )
    self     . setBustle             (                                       )
    ##########################################################################
    LOID     = self . LOID
    UUID     = 0
    LTYPE    = 0
    BODY     = None
    JSOX     =                       {                                       }
    LODTAB   = self . Tables         [ "LOD"                                 ]
    ##########################################################################
    QQ       = f"""select
                   `uuid` , `type` , `parameters` , `body`
                   from {LODTAB}
                   where ( `id` = {LOID} ) ;"""
    QQ       = " " . join            ( QQ . split ( )                        )
    ##########################################################################
    DB       . Query                 ( QQ                                    )
    RR       = DB . FetchOne         (                                       )
    ##########################################################################
    if ( ( RR not in [ False , None ] ) and ( len ( RR ) == 4 ) )            :
      ########################################################################
      UUID   = int                   ( RR [ 0                              ] )
      LTYPE  = int                   ( RR [ 1                              ] )
      JSOX   = self . toJSON         ( RR [ 2                              ] )
      BODY   =                         RR [ 3                                ]
      ########################################################################
      if ( ( BODY not in [ False , None ] ) and ( len ( BODY ) > 0 ) )       :
        ######################################################################
        OKAY = True
    ##########################################################################
    self     . setVacancy            (                                       )
    self     . GoRelax . emit        (                                       )
    self     . ShowStatus            ( ""                                    )
    DB       . Close                 (                                       )
    ##########################################################################
    if                               ( OKAY                                ) :
      ########################################################################
      self   . Uuid = UUID
      ########################################################################
      if                             ( 102 == LTYPE                        ) :
        ######################################################################
        self . LoadBareWaveFront     ( JSOX , BODY                           )
      ########################################################################
      elif                           ( 112 == LTYPE                        ) :
        ######################################################################
        self . LoadZipWaveFront      ( JSOX , BODY                           )
    ##########################################################################
    self . emitStartModel . emit     (                                       )
    ##########################################################################
    return
  ############################################################################
