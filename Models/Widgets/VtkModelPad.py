  def toJSON               ( self , TEXT                                   ) :
    ##########################################################################
    if                     ( len ( TEXT ) <= 0                             ) :
      return               {                                                 }
    ##########################################################################
    try                                                                      :
      BODY = TEXT . decode ( "utf-8"                                         )
    except                                                                   :
      return               {                                                 }
    ##########################################################################
    if                     ( len ( BODY ) <= 0                             ) :
      return               {                                                 }
    ##########################################################################
    return json . loads    ( BODY                                            )
