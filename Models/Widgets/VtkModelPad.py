    ##########################################################################
    mm     . addSeparator       (                                            )
    ##########################################################################
    self   . ModelMenu          ( mm                                         )
    self   . FilesMenu          ( mm                                         )
    mm     . addSeparator       (                                            )
    self   . DockingMenu        ( mm                                         )
    ##########################################################################
    mm     . setFont            ( self    . menuFont ( )                     )
    aa     = mm . exec_         ( QCursor . pos      ( )                     )
    at     = mm . at            ( aa                                         )
    ##########################################################################
    if                          ( self . RunModelMenu ( at               ) ) :
      return True
    ##########################################################################
    if                          ( self . RunFilesMenu ( at               ) ) :
      return True
    ##########################################################################
    if                          ( self . RunDocking   ( mm , aa          ) ) :
      return True
    ##########################################################################
    if                          ( at == 1101                               ) :
      ########################################################################
      self . SwitchPad          (                                            )
      ########################################################################
      return True
    ##########################################################################
    if                          ( at == 1102                               ) :
      ########################################################################
      self . SwitchMouse        (                                            )
      ########################################################################
      return True
