  def acValue                    ( self  , Balls , Numbers                 ) :
    ##########################################################################
    DIFF       =                 [                                           ]
    All        = 0
    ##########################################################################
    for   i in range             ( 0     , Balls - 1                       ) :
      N        = Numbers         [ i                                         ]
      for j in range             ( i + 1 , Balls                           ) :
        X      = Numbers         [ j                                         ]
        D      = int             ( X - N                                     )
        if                       ( D < 0                                   ) :
          D    = - D
        if                       ( D not in DIFF                           ) :
          DIFF . append          ( D                                         )
          All  = len             ( DIFF                                      )
    ##########################################################################
    return                       ( All - Balls + 1                           )
