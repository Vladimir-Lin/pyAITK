  def numberGap                  ( self , Balls , Numbers                  ) :
    ##########################################################################
    MinV      = 50
    MaxV      =  0
    ##########################################################################
    for i in range               ( 0    , Balls                            ) :
      V       = int              ( Numbers [ i ]                             )
      if                         ( V > MaxV                                ) :
        MaxV  = V
      if                         ( V < MinV                                ) :
        MinV  = V
    ##########################################################################
    return int                   ( MaxV - MinV                               )
