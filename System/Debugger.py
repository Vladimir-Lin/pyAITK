## 轉換除錯等級
##############################################################################
def GetDebuggerLevel ( DBG                                                 ) :
  ############################################################################
  LEVEL     = logging . NOTSET
  ############################################################################
  if                 ( "NOTSET"   == DBG                                   ) :
    ##########################################################################
    LEVEL = logging . NOTSET
  ############################################################################
  elif               ( "DEBUG"    == DBG                                   ) :
    ##########################################################################
    LEVEL = logging . DEBUG
  ############################################################################
  elif               ( "INFO"     == DBG                                   ) :
    ##########################################################################
    LEVEL = logging . INFO
  ############################################################################
  elif               ( "WARNING"  == DBG                                   ) :
    ##########################################################################
    LEVEL = logging . WARNING
  ############################################################################
  elif               ( "ERROR"    == DBG                                   ) :
    ##########################################################################
    LEVEL = logging . ERROR
  ############################################################################
  elif               ( "CRITICAL" == DBG                                   ) :
    LEVEL = logging . CRITICAL
  ############################################################################
  return LEVEL
##############################################################################
## 改變除錯等級
##############################################################################
def ChangeDebuggerLevel        ( DBG                                       ) :
  ############################################################################
  LEVEL  = GetDebuggerLevel    ( DBG                                         )
  Logger = logging . getLogger (                                             )
  Logger . setLevel            ( LEVEL                                       )
  ############################################################################
  return
##############################################################################
## 設置紀錄器
##############################################################################
def ConfigureDebugger          ( settings = { }                            ) :
  ############################################################################
  DBG          = "NOTSET"
  LOX          = ""
  isConsole    = False
  LEVEL        = logging . NOTSET
  ############################################################################
  if                           ( "Debug" in settings                       ) :
    DBG        = settings      [ "Debug"                                     ]
  ############################################################################
  if                           ( "LOG"   in settings                       ) :
    LOX        = settings      [ "LOG"                                       ]
  ############################################################################
  if                           ( "Console" in settings                     ) :
    isConsole  = settings      [ "Console"                                   ]
  ############################################################################
  LEVEL = GetDebuggerLevel     ( DBG                                         )
  ############################################################################
  ## 規劃除錯訊息
  ############################################################################
  MSGFMT       = u"%(asctime)s %(levelname)-8s %(message)s"
  DATEFMT      = "%Y/%m/%d %H:%M:%S"
  ############################################################################
  logging      . basicConfig                                                  (
    level      = LEVEL                                                        ,
    format     = MSGFMT                                                       ,
    datefmt    = DATEFMT                                                      )
  ############################################################################
  ## 設定記錄檔
  ############################################################################
  if                           ( len ( LOX ) > 0                           ) :
    ##########################################################################
    Logger     = logging . getLogger   (                                     )
    ##########################################################################
    fileh      = logging . FileHandler ( LOX , mode = 'a' , encoding='utf-8' )
    ##########################################################################
    if                                 ( not isConsole                     ) :
      for hdlr in Logger . handlers    [ :                                 ] :
        Logger . removeHandler         ( hdlr                                )
    ##########################################################################
    formatter  = logging . Formatter   ( MSGFMT , DATEFMT                    )
    fileh      . setFormatter          ( formatter                           )
    Logger     . addHandler            ( fileh                               )
    ##########################################################################
  return
##############################################################################
## 顯示訊息
##############################################################################
def LOG                        ( Message                                   ) :
  ############################################################################
  Logger = logging . getLogger (                                             )
  Logger . info                ( Message                                     )
  ############################################################################
  return
