##############################################################################
class Brush                ( QBrush                                        ) :
  ############################################################################
  def __init__             ( self                                          ) :
    ##########################################################################
    super ( ) . __init__   (                                                 )
    self      . Initialize (                                                 )
    ##########################################################################
    return
  ############################################################################
  def __del__              ( self                                          ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  def Initialize           ( self                                          ) :
    ##########################################################################
    self . Uuid     = 0
    self . Name     = ""
    self . Editable = True
    ##########################################################################
    return
  ############################################################################
  def Configuration                           ( self                       ) :
    ##########################################################################
    c                     = self . color      (                              )
    JSON                  =                   {                              }
    UUID                  = self . Uuid
    ##########################################################################
    JSON [ "Type"     ] = "Brush"
    JSON [ "Uuid"     ] = f"{UUID}"
    JSON [ "Name"     ] = self . Name
    JSON [ "Editable" ] = self . Editable
    JSON [ "Style"    ] = self . style      (                              )
    JSON [ "R"        ] = c    . red        (                              )
    JSON [ "G"        ] = c    . green      (                              )
    JSON [ "B"        ] = c    . blue       (                              )
    JSON [ "A"        ] = c    . alpha      (                              )
    ##########################################################################
    return JSON
  ############################################################################
  def setConfigure         ( self , JSON                                   ) :
    ##########################################################################
    self . Uuid     = int  ( JSON [ "Uuid" ]                                 )
    self . Name     = JSON [ "Name"                                          ]
    self . Editable = JSON [ "Editable"                                      ]
    ##########################################################################
    self . setStyle        ( JSON [ "Style"                                ] )
    ##########################################################################
    R    = int             ( JSON [ "R"                                    ] )
    G    = int             ( JSON [ "G"                                    ] )
    B    = int             ( JSON [ "B"                                    ] )
    A    = int             ( JSON [ "A"                                    ] )
    self . setColor        ( QColor ( R , G , B , A )                        )
    ##########################################################################
    return
