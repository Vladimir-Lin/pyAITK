##############################################################################
class Font                 ( QFont                                         ) :
  ############################################################################
  def __init__             ( self                                          ) :
    ##########################################################################
    super ( ) . __init__   (                                                 )
    self      . Initialize (                                                 )
    ##########################################################################
    return
  ############################################################################
  def __del__         ( self                                               ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  def Initialize            ( self                                         ) :
    ##########################################################################
    self . Uuid     = 0
    self . Name     = ""
    self . CM       = 0.40
    self . Editable = True
    self . setStyleStrategy ( self . PreferAntialias                         )
    ##########################################################################
    return
  ############################################################################
  def Configuration        ( self                                          ) :
    ##########################################################################
    JSON =                 {                                                 }
    DESC = self . toString (                                                 )
    UUID = self . Uuid
    ##########################################################################
    JSON [ "Uuid"        ] = f"{UUID}"
    JSON [ "Name"        ] = self . Name
    JSON [ "CM"          ] = self . CM
    JSON [ "Editable"    ] = self . Editable
    JSON [ "Description" ] = DESC
    ##########################################################################
    return JSON
  ############################################################################
  def setConfigure          ( self , JSON                                  ) :
    ##########################################################################
    self . Uuid     = int   ( JSON [ "Uuid" ]                                )
    self . Name     = JSON  [ "Name"                                         ]
    self . CM       = float ( JSON [ "CM"   ]                                )
    self . Editable = JSON  [ "Editable"                                     ]
    ##########################################################################
    DESC = JSON             [ "Description"                                  ]
    self . fromString       ( DESC                                           )
    ##########################################################################
    return
  ############################################################################
  def setDPI            ( self , DPI                                       ) :
    ##########################################################################
    ps   = int          ( self . CM * float ( DPI ) * 100.0 / 254.0          )
    self . setPixelSize ( ps                                                 )
    ##########################################################################
    return
  ############################################################################
  def assign          ( self , font                                        ) :
    ##########################################################################
    self . fromString ( font . toString ( )                                  )
    ##########################################################################
    return
