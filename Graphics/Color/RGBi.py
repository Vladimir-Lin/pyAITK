## RGBi
## RGB顏色模型 0~255整數值域
##############################################################################
import cv2
import numpy as np
import colour
from . CommonColor import CommonColor
##############################################################################
class RGBi                  ( CommonColor                                  ) :
  ############################################################################
  def __init__              ( self                                         ) :
    ##########################################################################
    self . ColorModel = "RGB"
    self . ValueType  = "UInt8"
    ##########################################################################
    self . R          = int ( 0                                              )
    self . G          = int ( 0                                              )
    self . B          = int ( 0                                              )
    ##########################################################################
    return
  ############################################################################
  def __init__              ( self , R , G , B                             ) :
    ##########################################################################
    self . ColorModel = "RGB"
    self . ValueType  = "UInt8"
    ##########################################################################
    self . R          = int ( R                                              )
    self . G          = int ( G                                              )
    self . B          = int ( B                                              )
    ##########################################################################
    return
  ############################################################################
  def __del__    ( self                                                    ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  ## 轉成JSON格式
  ############################################################################
  def toJson ( self                                                        ) :
    return   { "Model" : "RGB"                                             , \
               "Value" : "UInt8"                                           , \
               "R"     : self . R                                          , \
               "G"     : self . G                                          , \
               "B"     : self . B                                            }
  ############################################################################
  ## 從JSON格式轉換顏色模型
  ############################################################################
  def fromJson     ( self , JSON                                           ) :
    ##########################################################################
    KEYs =         [ "Model" , "Value" , "R" , "G" , "B"                     ]
    ##########################################################################
    for K in KEYs                                                            :
      if           ( K not in JSON                                         ) :
        return False
    ##########################################################################
    if             ( "RGB"   != JSON [ "Model" ]                           ) :
      return   False
    ##########################################################################
    if             ( "UInt8" != JSON [ "Value" ]                           ) :
      return   False
    ##########################################################################
    self . R = int ( JSON [ "R" ]                                            )
    self . G = int ( JSON [ "G" ]                                            )
    self . B = int ( JSON [ "B" ]                                            )
    ##########################################################################
    return     True
  ############################################################################
  def fromRGBf                   ( self , fRGB                             ) :
    ##########################################################################
    self . R = self . ScaleUInt8 (        fRGB . R                           )
    self . G = self . ScaleUInt8 (        fRGB . G                           )
    self . B = self . ScaleUInt8 (        fRGB . B                           )
    ##########################################################################
    return True
  ############################################################################
  def fromRGBAi    ( self , iRGBA                                          ) :
    ##########################################################################
    self . R = int ( iRGBA . R                                               )
    self . G = int ( iRGBA . G                                               )
    self . B = int ( iRGBA . B                                               )
    ##########################################################################
    return True
  ############################################################################
  def toRGB ( self                                                         ) :
    return  [ self . R , self . G , self . G                                 ]
  ############################################################################
  def toBGR ( self                                                         ) :
    return  [ self . B , self . G , self . R                                 ]
  ############################################################################
  def toNpBGR         ( self                                               ) :
    return np . array ( [ [ self . toBGR ( ) ] ] , dtype = np . uint8        )
