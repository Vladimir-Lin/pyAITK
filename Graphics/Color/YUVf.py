import cv2
import numpy as np
import colour
from . CommonColor import CommonColor
##############################################################################
class YUVf                    ( CommonColor                                ) :
  ############################################################################
  def __init__                ( self                                       ) :
    ##########################################################################
    self . ColorModel = "YUV"
    self . ValueType  = "Double"
    ##########################################################################
    self . Y          = float ( 0.0                                          )
    self . U          = float ( 0.0                                          )
    self . V          = float ( 0.0                                          )
    ##########################################################################
    return
  ############################################################################
  def __del__    ( self                                                    ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  ## 轉成JSON格式
  ############################################################################
  def toJson ( self                                                        ) :
    return   { "Model" : "YUV"                                             , \
               "Value" : "Float"                                           , \
               "Y"     : self . Y                                          , \
               "U"     : self . U                                          , \
               "V"     : self . V                                            }
  ############################################################################
  ## 從JSON格式轉換顏色模型
  ############################################################################
  def fromJson       ( self , JSON                                         ) :
    ##########################################################################
    KEYs =           [ "Model" , "Value" , "Y" , "U" , "V"                   ]
    ##########################################################################
    for K in KEYs                                                            :
      if             ( K not in JSON                                       ) :
        return False
    ##########################################################################
    if               ( "YUV"   != JSON [ "Model" ]                         ) :
      return   False
    ##########################################################################
    if               ( "Float" != JSON [ "Value" ]                         ) :
      return   False
    ##########################################################################
    self . Y = float ( JSON [ "Y" ]                                          )
    self . U = float ( JSON [ "U" ]                                          )
    self . V = float ( JSON [ "V" ]                                          )
    ##########################################################################
    return     True
  ############################################################################
  def fromYUVi                   ( self , iYUV                             ) :
    ##########################################################################
    self . Y = self . ScaleFloat (        iYUV . Y                           )
    self . U = self . ScaleFloat (        iYUV . U                           )
    self . V = self . ScaleFloat (        iYUV . V                           )
    ##########################################################################
    return
  ############################################################################
  def toYUV ( self                                                         ) :
    return  [ self . Y , self . U , self . V                                 ]
  ############################################################################
  def toNpYUV         ( self                                               ) :
    return np . array ( [ [ self . toYUV ( ) ] ] , dtype = np . float64      )
  ############################################################################
  def fromCvYUV      ( self , YUV                                          ) :
    ##########################################################################
    self . Y = float ( YUV [ 0 ]                                             )
    self . U = float ( YUV [ 1 ]                                             )
    self . V = float ( YUV [ 2 ]                                             )
    ##########################################################################
    return
  ############################################################################
  def Distance          ( self ,                       C                   ) :
    ##########################################################################
    NP = self . toNpYUV (                                                    )
    ##########################################################################
    return np . sqrt    ( np . sum ( ( NP [ 0 ][ 0 ] - C ) ** 2            ) )
