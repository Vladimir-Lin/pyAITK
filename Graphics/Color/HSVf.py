##############################################################################
## HSVf
## HSV顏色模型 0~1浮點值域
##############################################################################
import cv2
import numpy as np
import colour
from . CommonColor import CommonColor
##############################################################################
class HSVf                    ( CommonColor                                ) :
  ############################################################################
  def __init__                ( self                                       ) :
    ##########################################################################
    self . ColorModel = "HSV"
    self . ValueType  = "Float"
    ##########################################################################
    self . H          = float ( 0.0                                          )
    self . S          = float ( 0.0                                          )
    self . V          = float ( 0.0                                          )
    ##########################################################################
    return
  ############################################################################
  def __del__    ( self                                                    ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  ## 轉成JSON格式
  ############################################################################
  def toJson ( self                                                        ) :
    return   { "Model" : "HSV"                                             , \
               "Value" : "Float"                                           , \
               "H"     : self . H                                          , \
               "S"     : self . S                                          , \
               "V"     : self . V                                            }
  ############################################################################
  ## 從JSON格式轉換顏色模型
  ############################################################################
  def fromJson       ( self , JSON                                         ) :
    ##########################################################################
    KEYs =           [ "Model" , "Value" , "H" , "S" , "V"                   ]
    ##########################################################################
    for K in KEYs                                                            :
      if             ( K not in JSON                                       ) :
        return False
    ##########################################################################
    if               ( "HSV"   != JSON [ "Model" ]                         ) :
      return   False
    ##########################################################################
    if               ( "Float" != JSON [ "Value" ]                         ) :
      return   False
    ##########################################################################
    self . H = float ( JSON [ "H" ]                                          )
    self . S = float ( JSON [ "S" ]                                          )
    self . V = float ( JSON [ "V" ]                                          )
    ##########################################################################
    return     True
  ############################################################################
  def fromHSVi     ( self , iHSV                                           ) :
    ##########################################################################
    self . H = int ( float ( iHSV . R ) / 255.0                              )
    self . S = int ( float ( iHSV . G ) / 255.0                              )
    self . V = int ( float ( iHSV . B ) / 255.0                              )
    ##########################################################################
    if             ( self . H < 0.0                                        ) :
      self . H = 0.0
    ##########################################################################
    if             ( self . S < 0.0                                        ) :
      self . S = 0.0
    ##########################################################################
    if             ( self . V < 0.0                                        ) :
      self . V = 0.0
    ##########################################################################
    if             ( self . H > 1.0                                        ) :
      self . H = 1.0
    ##########################################################################
    if             ( self . S > 1.0                                        ) :
      self . S = 1.0
    ##########################################################################
    if             ( self . V > 1.0                                        ) :
      self . V = 1.0
    ##########################################################################
    return
