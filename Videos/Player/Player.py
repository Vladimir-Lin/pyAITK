  def RunAnalysis ( self                                                   ) :
    ##########################################################################
    if            ( self . isAnalyzing                                     ) :
      return
    ##########################################################################
    self . isAnalyzing = True
    self . ATS         =       [                                             ]
    self . ATJ         =       [                                             ]
    ##########################################################################
    self . PANEL . Analysis . hide (                                         )
    ##########################################################################
    self . NextAnalysis . emit (                                             )
    ##########################################################################
    return
