class Descriptive (                                                        ) :
  ############################################################################
  def __init__   ( self                                                    ) :
    ##########################################################################
    self . Clear (                                                           )
    ##########################################################################
    return
  ############################################################################
  def __del__ ( self                                                       ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  def Clear             ( self                                             ) :
    ##########################################################################
    self . BaseTime   = 0
    self . Duration   = 0
    self . Locality   = 1001
    ##########################################################################
    self . TIMESTAMPs = [                                                    ]
    self . CONTEXTs   = {                                                    }
    self . SEGMENTs   = [                                                    ]
    ##########################################################################
    return
  ############################################################################
  def shiftTime ( self , T                                                 ) :
    return int  ( int ( T ) + self . BaseTime                                )
  ############################################################################
  def realTime ( self , T                                                  ) :
    return int ( int ( T ) - self . BaseTime                                 )
  ############################################################################
  def setLocality ( self , locality                                        ) :
    ##########################################################################
    self . Locality = locality
    ##########################################################################
    return
  ############################################################################
  def toJson          ( self                                               ) :
    ##########################################################################
    CT              = {                                                      }
    SG              = [                                                      ]
    ##########################################################################
    for T in self . TIMESTAMPs                                               :
      ########################################################################
      CT [ f"{T}" ] = self . CONTEXTs [ T ] . toJson (                       )
    ##########################################################################
    for S in self . SEGMENTs                                                 :
      ########################################################################
      SG            . append                         ( S . toJson (        ) )
    ##########################################################################
    return            { "Timestamps" : self . TIMESTAMPs                   , \
                        "Contexts"   : CT                                  , \
                        "Segments"   : SG                                    }
  ############################################################################
  def setJson                     ( self , JSON                            ) :
    ##########################################################################
    if                            ( "Timestamps" in JSON                   ) :
      ########################################################################
      self . TIMESTAMPs = JSON    [ "Timestamps"                             ]
    ##########################################################################
    if                            ( "Contexts" in JSON                     ) :
      ########################################################################
      self . CONTEXTs   =         {                                          }
      ########################################################################
      for T in self . TIMESTAMPs                                             :
        ######################################################################
        AC              = Anchor  (                                          )
        AC              . setJson ( JSON [ "Contexts" ] [ f"{T}" ]           )
        self . CONTEXTs [ T ] = AC
    ##########################################################################
    if                            ( "Segments" in JSON                     ) :
      ########################################################################
      for S in JSON               [ "Segments"                             ] :
        ######################################################################
        SG              = Segment (                                          )
        SG              . setJson ( S                                        )
        self . SEGMENTs . append  ( SG                                       )
    ##########################################################################
    return
  ############################################################################
  def toScenario ( self                                                    ) :
    return       { "BaseTime"    : self . BaseTime                         , \
                   "Duration"    : self . Duration                         , \
                   "Locality"    : self . Locality                         , \
                   "Description" : self . toJson (                         ) }
  ############################################################################
  def setScenario            ( self , JSON                                 ) :
    ##########################################################################
    self   . Clear           (                                               )
    ##########################################################################
    if                       ( "BaseTime"    in JSON                       ) :
      self . BaseTime = int  ( JSON [ "BaseTime"                           ] )
    ##########################################################################
    if                       ( "Duration"    in JSON                       ) :
      self . Duration = int  ( JSON [ "Duration"                           ] )
    ##########################################################################
    if                       ( "Locality"    in JSON                       ) :
      self . Locality = int  ( JSON [ "Locality"                           ] )
    ##########################################################################
    if                       ( "Description" in JSON                       ) :
      self . setJson         ( JSON [ "Description"                        ] )
    ##########################################################################
    return
  ############################################################################
  def Replace               ( self , fromTime , asTime                     ) :
    ##########################################################################
    if                      ( fromTime not in self . TIMESTAMPs            ) :
      return
    ##########################################################################
    V     =                 [                                                ]
    ##########################################################################
    for T in self . TIMESTAMPs                                               :
      ########################################################################
      if                    ( fromTime != T                                ) :
        V . append          ( T                                              )
    ##########################################################################
    V     . append          ( asTime                                         )
    V     . sort            (                                                )
    ##########################################################################
    self  . TIMESTAMPs = V
    ##########################################################################
    KV    = self . CONTEXTs [ fromTime                                       ]
    self  . CONTEXTs . pop  ( fromTime , None                                )
    ##########################################################################
    KV    . Replace         ( fromTime , asTime                              )
    self  . CONTEXTs [ asTime ] = KV
    ##########################################################################
    for S in self . SEGMENTs                                                 :
      S   . Replace         ( fromTime , asTime                              )
    ##########################################################################
    return
  ############################################################################
  def setContext ( self , ts , message                                     ) :
    ##########################################################################
    if           ( ts not in self . TIMESTAMPs                             ) :
      return
    ##########################################################################
    if           ( ts not in self . CONTEXTs                               ) :
      return
    ##########################################################################
    self . CONTEXTs [ ts ] . setContext ( self . Locality , message          )
    ##########################################################################
    return
  ############################################################################
  def getContext ( self , ts                                               ) :
    ##########################################################################
    if           ( ts not in self . TIMESTAMPs                             ) :
      return ""
    ##########################################################################
    if           ( ts not in self . CONTEXTs                               ) :
      return ""
    ##########################################################################
    return self . CONTEXTs [ ts ] . getContext ( self . Locality             )
  ############################################################################
  def addItem                  ( self , ts , text                          ) :
    ##########################################################################
    if                         ( ts in self . TIMESTAMPs                   ) :
      return
    ##########################################################################
    AC = Anchor                (                                             )
    AC . setTimestamp          ( ts                                          )
    AC . setContext            ( self . Locality , text                      )
    ##########################################################################
    self . CONTEXTs [ ts ] = AC
    ##########################################################################
    self . TIMESTAMPs . append ( ts                                          )
    self . TIMESTAMPs . sort   (                                             )
    ##########################################################################
    return
  ############################################################################
  def deleteItem          ( self , ts                                      ) :
    ##########################################################################
    if                    ( ts not in self . TIMESTAMPs                    ) :
      return
    ##########################################################################
    SG     =              [                                                  ]
    ##########################################################################
    for S in self . SEGMENTs                                                 :
      ########################################################################
      if                  ( not S . Contains ( ts                        ) ) :
        ######################################################################
        SG . append       ( S                                                )
    ##########################################################################
    self   . SEGMENTs = SG
    ##########################################################################
    try                                                                      :
      del self . CONTEXTs [ ts                                               ]
    except                                                                   :
      pass
    ##########################################################################
    TT     =              [                                                  ]
    ##########################################################################
    for T in self . TIMESTAMPs                                               :
      ########################################################################
      if                  ( ts != T                                        ) :
        TT . append       ( T                                                )
    ##########################################################################
    TT     . sort         (                                                  )
    self   . TIMESTAMPs = TT
    ##########################################################################
    return
  ############################################################################
  def itemJson ( self  , ts                                                ) :
    ##########################################################################
    if         ( ts not in self . TIMESTAMPs                               ) :
      return   ( False , {                                                 } )
    ##########################################################################
    if         ( ts not in self . CONTEXTs                                 ) :
      return   ( False , {                                                 } )
    ##########################################################################
    JJ             = self . CONTEXTs  [ ts ] . toJson     (                  )
    JJ [ "Name"  ] = self . CONTEXTs  [ ts ] . getContext ( self . Locality  )
    JJ [ "Shift" ] = self . shiftTime ( ts                                   )
    ##########################################################################
    return     ( True  , JJ ,                                                )
  ############################################################################
  def LastestTimestamp ( self , TimeGap                                    ) :
    ##########################################################################
    if                 ( len ( self . TIMESTAMPs ) <= 0                    ) :
      return 0
    ##########################################################################
    return int         ( self . TIMESTAMPs [ -1 ] + TimeGap                  )
  ############################################################################
  def Clone ( self , sourceLocality , targetLocality                       ) :
    ##########################################################################
    for T in self . TIMESTAMPs                                               :
      ########################################################################
      self . CONTEXTs [ T ] . Clone ( sourceLocality , targetLocality        )
    ##########################################################################
    return
  ############################################################################
  def setOption                       ( self , T , KEY , V                 ) :
    ##########################################################################
    if                                ( T not in self . CONTEXTs           ) :
      return
    ##########################################################################
    self . CONTEXTs [ T ] . setOption ( KEY , V                              )
    ##########################################################################
    return
  ############################################################################
  def getOption                              ( self , T , KEY              ) :
    ##########################################################################
    if                                       ( T not in self . CONTEXTs    ) :
      return False
    ##########################################################################
    return self . CONTEXTs [ T ] . getOption ( KEY                           )
  ############################################################################
  def OptionString ( self , T                                              ) :
    ##########################################################################
    if             ( T not in self . CONTEXTs                              ) :
      return ""
    ##########################################################################
    return self . CONTEXTs [ T ] . OptionString (                            )
