class Anchor     (                                                         ) :
  ############################################################################
  def __init__   ( self                                                    ) :
    ##########################################################################
    self . Clear (                                                           )
    ##########################################################################
    return
  ############################################################################
  def __del__ ( self                                                       ) :
    ##########################################################################
    ##########################################################################
    return
  ############################################################################
  def Clear            ( self                                              ) :
    ##########################################################################
    self . Timestamp = -1
    self . Action    = 0
    self . States    = 0
    self . NAMEs     = {                                                     }
    self . OPTIONs   = {                                                     }
    self . CROWDs    = [                                                     ]
    ##########################################################################
    return
  ############################################################################
  def toJson ( self                                                        ) :
    return   { "Timestamp" : self . Timestamp                              , \
               "Action"    : self . Action                                 , \
               "States"    : self . States                                 , \
               "Names"     : self . NAMEs                                  , \
               "Options"   : self . OPTIONs                                , \
               "Crowds"    : self . CROWDs                                   }
  ############################################################################
  def setJson                 ( self , JSON                                ) :
    ##########################################################################
    if                        ( "Timestamp" in JSON                        ) :
      self . Timestamp = int  ( JSON [ "Timestamp"                         ] )
    ##########################################################################
    if                        ( "Action"    in JSON                        ) :
      self . Action    = int  ( JSON [ "Action"                            ] )
    ##########################################################################
    if                        ( "States"    in JSON                        ) :
      self . States    = int  ( JSON [ "States"                            ] )
    ##########################################################################
    if                        ( "Names"     in JSON                        ) :
      self . NAMEs     = JSON [ "Names"                                      ]
    ##########################################################################
    if                        ( "Options"   in JSON                        ) :
      self . OPTIONs   = JSON [ "Options"                                    ]
    ##########################################################################
    if                        ( "Crowds"    in JSON                        ) :
      self . CROWDs    = JSON [ "Crowds"                                     ]
    ##########################################################################
    return
  ############################################################################
  def setTimestamp         ( self , ts                                     ) :
    ##########################################################################
    self . Timestamp = int (        ts                                       )
    ##########################################################################
    return
  ############################################################################
  def getTimestamp ( self , baseTime = 0                                   ) :
    return int     ( int ( baseTime ) + self . Timestamp                     )
  ############################################################################
  def setAction         ( self , action                                    ) :
    ##########################################################################
    self . Action = int (        action                                      )
    ##########################################################################
    return
  ############################################################################
  def setStates         ( self , states                                    ) :
    ##########################################################################
    self . States = int (        states                                      )
    ##########################################################################
    return
  ############################################################################
  def setContext ( self , Locality , Context ) :
    ##########################################################################
    self . NAMEs [ f"{Locality}" ] = f"{Context}"
    ##########################################################################
    return
  ############################################################################
  def getContext          ( self , Locality                                ) :
    ##########################################################################
    KEY = str             ( Locality                                         )
    ##########################################################################
    if                    ( KEY in self . NAMEs                            ) :
      return self . NAMEs [ KEY                                              ]
    ##########################################################################
    return ""
  ############################################################################
  def Replace                ( self , fromTime , asTime                    ) :
    ##########################################################################
    if                       ( int ( fromTime ) == self . Timestamp        ) :
      self . Timestamp = int ( asTime                                        )
    ##########################################################################
    return
  ############################################################################
  def Clone ( self , sourceLocality , targetLocality                       ) :
    ##########################################################################
    SL = f"{sourceLocality}"
    TL = f"{targetLocality}"
    ##########################################################################
    if      ( SL     in self . NAMEs                                       ) :
      ########################################################################
      if    ( TL not in self . NAMEs                                       ) :
        self . NAMEs [ TL ] = self . NAMEs [ SL                              ]
    ##########################################################################
    return
  ############################################################################
  def setOption ( self , KEY , V                                           ) :
    ##########################################################################
    self . OPTIONs [ KEY ] = V
    ##########################################################################
    return
  ############################################################################
  def getOption           ( self , KEY                                     ) :
    ##########################################################################
    if                    ( KEY not in self . OPTIONs                      ) :
      return False
    ##########################################################################
    return self . OPTIONs [ KEY                                              ]
  ############################################################################
  def OptionString ( self                                                  ) :
    ##########################################################################
    OPTs     = ""
    ##########################################################################
    if             ( "Chapter"   in self . OPTIONs                         ) :
      ########################################################################
      if           ( self . OPTIONs [ "Chapter"   ]                        ) :
        ######################################################################
        M    = "◎"
        OPTs = f"{OPTs}{M}"
    ##########################################################################
    if             ( "Paragraph" in self . OPTIONs                         ) :
      ########################################################################
      if           ( self . OPTIONs [ "Paragraph" ]                        ) :
        ######################################################################
        M    = "◆"
        OPTs = f"{OPTs}{M}"
    ##########################################################################
    if             ( "Subtitle"  in self . OPTIONs                         ) :
      ########################################################################
      if           ( self . OPTIONs [ "Subtitle"  ]                        ) :
        ######################################################################
        M    = "※"
        OPTs = f"{OPTs}{M}"
    ##########################################################################
    return OPTs
