  def loading                         ( self                               ) :
    ##########################################################################
    self       . OnBusy  . emit       (                                      )
    self       . setBustle            (                                      )
    ##########################################################################
    DB         = self . ConnectDB     (                                      )
    ##########################################################################
    if                                ( self . NotOkay ( DB              ) ) :
      ########################################################################
      self     . setVacancy           (                                      )
      self     . GoRelax . emit       (                                      )
      self     . Notify               ( 1                                    )
      ########################################################################
      return
    ##########################################################################
    FMT        = self . Translations  [ "UI::StartLoading"                   ]
    MSG        = FMT . format         ( self . windowTitle (               ) )
    self       . ShowStatus           ( MSG                                  )
    ##########################################################################
    POSTAB     = self . Tables        [ "Posture"                            ]
    self       . JSON =               { "Keys" : [ ] , "Translations" : { }  }
    ##########################################################################
    QQ         = f"""select `name` , `comment` , `description` from {POSTAB}
                     order by `name` asc ;"""
    DB         . Query                ( " " . join ( QQ . split ( ) )        )
    RR         = DB . FetchAll        (                                      )
    ##########################################################################
    if                                ( len ( RR ) > 0                     ) :
      ########################################################################
      for R in RR                                                            :
        ######################################################################
        if                            ( 3 == len ( R )                     ) :
          ####################################################################
          NC   = R                    [ 0                                    ]
          CC   = R                    [ 1                                    ]
          DC   = R                    [ 2                                    ]
          ####################################################################
          try                                                                :
            ##################################################################
            NU = NC   . decode        ( "utf-8"                              )
            CU = CC   . decode        ( "utf-8"                              )
            DU = DC   . decode        ( "utf-8"                              )
            DJ = json . loads         ( DU                                   )
            ##################################################################
            TP =                      { "Comment" : CU , "Actions" : DJ      }
            ##################################################################
            self      . JSON          [ "Keys" ] . append ( NU               )
            self      . JSON          [ "Translations" ]  [ NU ] = TP
            ##################################################################
          except                                                             :
            ##################################################################
            pass
    ##########################################################################
    DB         . Close                (                                      )
    self       . setVacancy           (                                      )
    self       . GoRelax . emit       (                                      )
    self       . ShowStatus           ( ""                                   )
