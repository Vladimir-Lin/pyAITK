  def RemoveKeys              ( self , DITEMs                              ) :
    ##########################################################################
    if                        ( len ( DITEMs ) <= 0                        ) :
      return
    ##########################################################################
    self   . OnBusy  . emit   (                                              )
    self   . setBustle        (                                              )
    ##########################################################################
    DB     = self . ConnectDB (                                              )
    ##########################################################################
    if                        ( self . NotOkay ( DB                      ) ) :
      ########################################################################
      self . setVacancy       (                                              )
      self . GoRelax . emit   (                                              )
      self . Notify           ( 1                                            )
      ########################################################################
      return
    ##########################################################################
    POSTAB = self . Tables    [ "Posture"                                    ]
    ##########################################################################
    for D in DITEMs                                                          :
      ########################################################################
      if                      ( D not in self . JSON [ "Keys" ]            ) :
        continue
      ########################################################################
      self . JSON [ "Keys" ] . remove ( f"{D}"                               )
      del self . JSON         [ "Translations" ] [ f"{D}"                    ]
      ########################################################################
      CKEY = D  . encode      ( "utf-8"                                      )
      ########################################################################
      QQ   = f"delete from {POSTAB} where ( `name` = %s ) ;"
      DB   . QueryValues      ( QQ , ( CKEY , )                              )
    ##########################################################################
    DB     . Close            (                                              )
    self   . setVacancy       (                                              )
    self   . GoRelax . emit   (                                              )
    self   . reload           (                                              )
    ##########################################################################
    return
