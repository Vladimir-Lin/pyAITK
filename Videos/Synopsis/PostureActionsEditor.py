  def nameChanged                 ( self                                   ) :
    ##########################################################################
    if                            ( not self . isItemPicked ( )            ) :
      return False
    ##########################################################################
    item     = self . CurrentItem [ "Item"                                   ]
    column   = self . CurrentItem [ "Column"                                 ]
    line     = self . CurrentItem [ "Widget"                                 ]
    text     = self . CurrentItem [ "Text"                                   ]
    msg      = line . text        (                                          )
    ##########################################################################
    if                            ( len ( msg ) <= 0                       ) :
      ########################################################################
      if                          ( len ( text ) <= 0                      ) :
        ######################################################################
        self . removeTopLevelItem ( item                                     )
      ########################################################################
      return
    ##########################################################################
    self     . removeParked       (                                          )
    ##########################################################################
    if                            ( text == msg                            ) :
      return
    ##########################################################################
    if                            ( 0 == column                            ) :
      ########################################################################
      if                          ( msg in self . JSON [ "Keys" ]          ) :
        ######################################################################
        item . setText            ( column , text                            )
        self . Notify             ( 1                                        )
        ######################################################################
        return
      ########################################################################
      item   . setText            ( column , msg                             )
      ########################################################################
      VAL    =                    ( text , msg ,                             )
      self   . Go                 ( self . UpdateKey , VAL                   )
    ##########################################################################
    elif                          ( 1 == column                            ) :
      ########################################################################
      item   . setText            ( column , msg                             )
      K      = item . text        ( 0                                        )
      VAL    =                    ( K , msg ,                                )
      self   . Go                 ( self . UpdateAction , VAL                )
    ##########################################################################
    elif                          ( 2 == column                            ) :
      ########################################################################
      item   . setText            ( column , msg                             )
      K      = item . text        ( 0                                        )
      VAL    =                    ( K , msg ,                                )
      self   . Go                 ( self . UpdateComment , VAL               )
    ##########################################################################
    return
