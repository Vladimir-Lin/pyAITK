  def UpdateKey        ( self , text , msg                                 ) :
    ##########################################################################
    NEWK          =    ( "" == text                                          )
    ##########################################################################
    if                 ( NEWK                                              ) :
      ########################################################################
      self        . JSON [ "Keys" ] . append ( f"{msg}"                      )
      ########################################################################
      TJS         =    { "Comment" : "" , "Actions" : { }                    }
      ########################################################################
      for L in self . KEYs                                                   :
        ######################################################################
        TJS [ "Actions" ] [ f"{L}" ] = ""
      ########################################################################
      self        . JSON [ "Translations" ] [ f"{msg}" ] = TJS
      ########################################################################
    else                                                                     :
      ########################################################################
      VKEYs       = self . JSON          [ "Keys"                            ]
      VKEYs       . remove               ( f"{text}"                         )
      VKEYs       . append               ( f"{msg}"                          )
      VKEYs       . sort                 (                                   )
      self        . JSON                 [ "Keys" ] = VKEYs
      ########################################################################
      TJS  = self . JSON [ "Translations" ] [ f"{text}"                      ]
      self        . JSON [ "Translations" ] [ f"{msg}" ] = TJS
      del self    . JSON [ "Translations" ] [ f"{text}"                      ]
    ##########################################################################
    self          . OnBusy  . emit       (                                   )
    self          . setBustle            (                                   )
    ##########################################################################
    DB            = self . ConnectDB     (                                   )
    ##########################################################################
    if                                   ( self . NotOkay ( DB           ) ) :
      ########################################################################
      self        . setVacancy           (                                   )
      self        . GoRelax . emit       (                                   )
      self        . Notify               ( 1                                 )
      ########################################################################
      return
    ##########################################################################
    POSTAB        = self . Tables        [ "Posture"                         ]
    ##########################################################################
    if                                   ( NEWK                            ) :
      ########################################################################
      NKEY        = msg  . encode        ( "utf-8"                           )
      DJS         = self . JSON [ "Translations" ] [ f"{msg}" ] [ "Actions"  ]
      ZJS         = json . dumps         ( DJS                               )
      UJS         = ZJS  . encode        ( "utf-8"                           )
      ########################################################################
      QQ          = f"""insert into {POSTAB}
                        ( `name` , `description` )
                        values ( %s , %s ) ;"""
      QQ          = " " . join           ( QQ . split ( )                    )
      DB          . QueryValues          ( QQ , ( NKEY , UJS , )             )
      ########################################################################
    else                                                                     :
      ########################################################################
      CKEY        = text . encode        ( "utf-8"                           )
      NKEY        = msg  . encode        ( "utf-8"                           )
      ########################################################################
      QQ          = f"""update {POSTAB}
                        set `name` = %s
                        where ( `name` = %s ) ;"""
      QQ          = " " . join           ( QQ . split ( )                    )
      DB          . QueryValues          ( QQ , ( NKEY , CKEY , )            )
    ##########################################################################
    DB            . Close                (                                   )
    self          . setVacancy           (                                   )
    self          . GoRelax . emit       (                                   )
    self          . ShowStatus           ( ""                                )
    self          . Notify               ( 5                                 )
    ##########################################################################
    return
