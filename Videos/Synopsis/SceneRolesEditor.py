  def ModifyId           ( self , item , NID , VID                         ) :
    ##########################################################################
    L      = self . JSON [ "Listings"                                        ]
    ##########################################################################
    if                   ( VID     in L                                    ) :
      ########################################################################
      self . Notify      ( 1                                                 )
      ########################################################################
      return
    ##########################################################################
    if                   ( NID not in L                                    ) :
      ########################################################################
      self . Notify      ( 1                                                 )
      ########################################################################
      return
    ##########################################################################
    self . JSON [ "Crowds" ] [ f"{VID}" ] = self . JSON [ "Crowds" ] [ f"{NID}" ]
    ##########################################################################
    L    . remove        ( NID                                               )
    L    . append        ( VID                                               )
    L    . sort          (                                                   )
    ##########################################################################
    self . JSON [ "Listings" ] = L
    ##########################################################################
    item . setData       ( 0 , Qt . UserRole , str ( VID )                   )
    item . setText       ( 0 ,                 str ( VID )                   )
    ##########################################################################
    self . JsonChanged   (                                                   )
    ##########################################################################
    return
