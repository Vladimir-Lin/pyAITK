  def AppendingNames            ( self , ITEM , UUIDs                      ) :
    ##########################################################################
    self   . OnBusy  . emit     (                                            )
    self   . setBustle          (                                            )
    ##########################################################################
    DB     = self . ConnectDB   (                                            )
    ##########################################################################
    if                          ( self . NotOkay ( DB )                    ) :
      ########################################################################
      self . setVacancy         (                                            )
      self . GoRelax . emit     (                                            )
      self . Notify             ( 1                                          )
      ########################################################################
      return
    ##########################################################################
    NAMTAB = self . Tables      [ "Names"                                    ]
    ##########################################################################
    DB     . LockWrites         ( [ NAMTAB                                 ] )
    ##########################################################################
    for UUID in UUIDs                                                        :
      ########################################################################
      NS   =                    { "Type" : ITEM , "UUID" : UUID              }
      ########################################################################
      for L in self . KEYs                                                   :
        ######################################################################
        NS [ f"{L}" ] = self . GetNameByLocality                           ( \
                                  DB                                       , \
                                  NAMTAB                                   , \
                                  UUID                                     , \
                                  L                                        , \
                                  "Default"                                  )
      ########################################################################
      NID  = self . NewPeopleId (                                            )
      self . JSON [ "Crowds" ]  [ f"{NID}" ] = NS
    ##########################################################################
    DB     . UnlockTables       (                                            )
    DB     . Close              (                                            )
    self   . setVacancy         (                                            )
    self   . GoRelax . emit     (                                            )
    self   . Notify             ( 5                                          )
    self   . JsonChanged        (                                            )
    self   . reload             (                                            )
    ##########################################################################
    return
