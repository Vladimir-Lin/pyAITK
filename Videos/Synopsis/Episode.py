  def UpdatePartsToGroups       ( self , DB , CLIPs , RELATE , KEY         ) :
    ##########################################################################
    if ( "Entities" not in self . Album [ "Clips" ]                        ) :
      return
    ##########################################################################
    DDIR    = self . DIR
    UUID    = self . Uuid
    RELTAB  = self . Tables     [ "VideoListings" ] [ "RelationVideos"       ]
    MKEY    = f"ClipsJoinGroup{KEY}"
    SMSG    = self . getMessage ( MKEY                                       )
    VUIDs   =                   [                                            ]
    MSGs    =                   [                                            ]
    ##########################################################################
    for F in CLIPs                                                           :
      ########################################################################
      if ( F      not in self . Album [ "Clips" ] [ "Entities" ]           ) :
        continue
      if ( "Uuid" not in self . Album [ "Clips" ] [ "Entities" ] [ F ]     ) :
        continue
      ########################################################################
      VUID  = self . Album [ "Clips" ] [ "Entities" ] [ F ] [ "Uuid"         ]
      ########################################################################
      if                        ( VUID <= 0                                ) :
        continue
      ########################################################################
      VUIDs . append            ( VUID                                       )
      ########################################################################
      PMSG  = SMSG
      PMSG  = PMSG . replace    ( "$(FILE)" , F                              )
      PMSG  = PMSG . replace    ( "$(UUID)" , f"{VUID}"                      )
      MSGs  . append            ( PMSG                                       )
    ##########################################################################
    if                          ( len ( VUIDs ) > 0                        ) :
      ########################################################################
      LQs   =                   [                                            ]
      CNT   = 0
      ########################################################################
      for VUID in VUIDs                                                      :
        ######################################################################
        LLL = f"( {UUID} , 76 , {VUID} , 11 , {RELATE} , {CNT} )"
        LQs . append            ( LLL                                        )
        CNT = CNT + 1
      ########################################################################
      LQ    = " , " . join      ( LQs                                        )
      GQ    = f"""insert into {RELTAB}
                  ( `first`,`t1`,`second`,`t2`,`relation`,`position` )
                  values
                  {LQ} ;"""
      GQ    = " " . join        ( GQ . split (                             ) )
    ##########################################################################
    DB      . LockWrites        ( [ RELTAB                                 ] )
    ##########################################################################
    QQ      = f"""delete from {RELTAB}
                  where ( `first` = {UUID} )
                    and ( `t1` = 76 )
                    and ( `t2` = 11 )
                    and ( `relation` = {RELATE} ) ;"""
    QQ      = " " . join ( QQ . split (                                    ) )
    self    . LOG               ( QQ                                         )
    DB      . Query             ( QQ                                         )
    ##########################################################################
    if                          ( len ( VUIDs ) > 0                        ) :
      self  . LOG               ( GQ                                         )
      DB    . Query             ( GQ                                         )
    ##########################################################################
    DB      . UnlockTables      (                                            )
    ##########################################################################
    for M in MSGs                                                            :
      ########################################################################
      self . LOG                ( M                                          )
    ##########################################################################
    return
