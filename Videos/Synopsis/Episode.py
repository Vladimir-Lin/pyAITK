    ##########################################################################
    if                           ( "FFprobe" in self . Settings            ) :
      ########################################################################
      PROBE  = self . Settings   [ "FFprobe"                                 ]
    ##########################################################################
    for VUID in VUIDs                                                        :
      ########################################################################
      FXD    = f"{VUID}"
      ########################################################################
      if ( FXD not in self . Album [ "Clips" ] [ "Information" ]           ) :
        continue
      ########################################################################
      if ( "Files" not in self . Album [ "Clips" ] [ "Information" ] [ FXD ] ) :
        continue
      ########################################################################
      FILEs  = self . Album [ "Clips" ] [ "Information" ] [ FXD ] [ "Files"  ]
      ########################################################################
      if                         ( len ( FILEs ) <= 0                      ) :
        continue
      ########################################################################
      FC     = FILEs             [ 0                                         ]
      RF     = f"{DDIR}/{FC}"
      ########################################################################
      if                         ( not Path ( RF ) . is_file ( )           ) :
        continue
      ########################################################################
      VFI    = FilmItem          (                                           )
      VJI    = VFI . Probe       ( RF , PROBE                                )
      ########################################################################
      if                         ( not VFI . Parse ( "" , VJI            ) ) :
        continue
      ########################################################################
      VFI    . Uuid = VUID
      OK     = VFI . Sync        ( DB , FIMTAB                               )
      ########################################################################
      if                         ( not OK                                  ) :
        continue
      ########################################################################
      self   . Album [ "Clips" ] [ "Information" ] [ FXD ] [ "Details" ] = VJI
      ########################################################################
      PMSG   = SMSG
      PMSG   = PMSG . replace    ( "$(FILE)" , FC                            )
      PMSG   = PMSG . replace    ( "$(UUID)" , FXD                           )
      self   . LOG               ( PMSG                                      )
    ##########################################################################
    return
