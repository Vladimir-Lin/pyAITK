  def GetEntityNamesFromDatabase ( self , DB , NAMTAB , UUID               ) :
    ##########################################################################
    NDBS   =                     {                                           }
    LCS    =                     [                                           ]
    ##########################################################################
    QQ     = f"""select `locality` from {NAMTAB}
                 where ( `uuid` = {UUID} )
                 group by `locality` asc ;"""
    QQ     = " " . join          ( QQ . split (                            ) )
    DB     . Query               ( QQ                                        )
    RR     = DB . FetchAll       (                                           )
    ##########################################################################
    if                           ( RR in [ False , None ]                  ) :
      return                     {                                           }
    ##########################################################################
    for R in RR                                                              :
      ########################################################################
      LC   = int                 ( R [ 0                                   ] )
      ########################################################################
      QQ   = f"""select `name` from {NAMTAB}
                 where ( `uuid` = {UUID} )
                   and ( `locality` = {LC} )
                 order by `priority` asc ;"""
      QQ   = " " . join          ( QQ . split (                            ) )
      DB   . Query               ( QQ                                        )
      NN   = DB . FetchAll       (                                           )
      ########################################################################
      if                         ( NN in [ False , None ]                  ) :
        return
      ########################################################################
      NS   =                     [                                           ]
      ########################################################################
      for N in NN                                                            :
        ######################################################################
        S  = self . toString     ( N [ 0                                   ] )
        ######################################################################
        if                       ( len ( S ) <= 0                          ) :
          continue
        ######################################################################
        NS . append              ( S                                         )
      ########################################################################
      if                         ( len ( NS ) <= 0                         ) :
        continue
      ########################################################################
      LCS  . append               ( LC                                       )
      NDBS [ f"{LC}" ] = NS
    ##########################################################################
    NDBS [ "Localities" ] = LCS
    ##########################################################################
    return NDBS
