def OpenMovieAlbumJson    ( DIR , VUID = ""                                ) :
  ############################################################################
  global ALBUMVERSION
  ############################################################################
  if                      ( not Path ( DIR ) . is_dir ( )                  ) :
    return ""
  ############################################################################
  GenerateAlbumFolder     ( DIR                                              )
  ############################################################################
  CWD       = os . getcwd (                                                  )
  os        . chdir       ( DIR                                              )
  os        . system      ( "move /y *.mp4 videos"                           )
  os        . system      ( "move /y *.mkv videos"                           )
  os        . system      ( "move /y *.avi videos"                           )
  os        . system      ( "move /y *.wmv videos"                           )
  os        . system      ( "move /y *.mpg videos"                           )
  os        . system      ( "move /y *.jpg images"                           )
  os        . system      ( "move /y *.jpeg images"                          )
  os        . system      ( "move /y *.png images"                           )
  os        . system      ( "move /y *.nfo scripts"                          )
  os        . system      ( "move /y *.torrent scripts"                      )
  os        . chdir       ( CWD                                              )
  ############################################################################
  AlbumJson = f"{DIR}/album.json"
  VXID      = f"{VUID}"
  ############################################################################
  if                      ( os . path . exists ( AlbumJson               ) ) :
    ##########################################################################
    if                    ( 19 == len ( VXID )                             ) :
      ########################################################################
      JFV   = LoadJson    ( AlbumJson                                        )
      AB    = ""
      ########################################################################
      if                  ( "Album" in JFV                                 ) :
        ######################################################################
        AB  = JFV         [ "Album"                                          ]
      ########################################################################
      if                  ( AB != VXID                                     ) :
        ######################################################################
        JFV [ "Album" ] = VXID
        ######################################################################
        SaveJson          ( AlbumJson , JFV                                  )
    ##########################################################################
    return AlbumJson
  ############################################################################
  NOW       = StarDate    (                                                  )
  NOW       . Now         (                                                  )
  FOLDER    = os . path . basename ( DIR                                     )
  JFV       =             { "Version"   : ALBUMVERSION                     , \
                            "Album"     : VXID                             , \
                            "Names"     : {                              } , \
                            "Directory" : DIR                              , \
                            "Folder"    : FOLDER                           , \
                            "Language"  : "zh-TW"                          , \
                            "File"      : AlbumJson                        , \
                            "Edited"    : False                            , \
                            "Timestamp" : NOW . Stardate                     }
  ############################################################################
  SaveJson                ( AlbumJson , JFV                                  )
  ############################################################################
  return AlbumJson
