def GenerateAlbumFolder  ( DIR                                             ) :
  ############################################################################
  if                     ( not Path ( DIR ) . is_dir ( )                   ) :
    return
  ############################################################################
  ROOTs    =             [ "DP"                                            , \
                           "Features"                                      , \
                           "images"                                        , \
                           "projects"                                      , \
                           "roles"                                         , \
                           "scripts"                                       , \
                           "subtitles"                                     , \
                           "videos"                                          ]
  SUBs     =             [ "CN" , "EN" , "JP" , "TW"                         ]
  ############################################################################
  for F in ROOTs                                                             :
    ##########################################################################
    D      = f"{DIR}/{F}"
    ##########################################################################
    if                   ( not Path ( D ) . is_dir ( )                     ) :
      ########################################################################
      try                                                                    :
        ######################################################################
        os . makedirs    ( D                                                 )
        ######################################################################
      except                                                                 :
        ######################################################################
        pass
  ############################################################################
  ST       = f"{DIR}/subtitles"
  ############################################################################
  if                     ( Path ( ST ) . is_dir ( )                        ) :
    ##########################################################################
    for S in SUBs                                                            :
      ########################################################################
      Z    = f"{ST}/{S}"
      ########################################################################
      if                 ( not Path ( Z ) . is_dir ( )                     ) :
        ######################################################################
        try                                                                  :
          ####################################################################
          os . makedirs  ( Z                                                 )
          ####################################################################
        except                                                               :
          ####################################################################
          pass
  ############################################################################
  return
