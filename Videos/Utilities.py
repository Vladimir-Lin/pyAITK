def M3UtoFilms                    ( M3U                                    ) :
  ############################################################################
  TEXT      = ""
  with open                       ( M3U , "rb" ) as F                        :
    TEXT    = F . read            (                                          )
  ############################################################################
  if                              ( len ( TEXT ) <= 0                      ) :
    return                        [                                          ]
  ############################################################################
  LM        = TEXT . find         ( b'# Playlist created by'                 )
  ############################################################################
  if                              ( LM > 0                                 ) :
    ##########################################################################
    TEXT    = TEXT                [ LM :                                     ]
    LM      = TEXT . find         ( b'\n'                                    )
    ##########################################################################
    if                            ( LM > 0                                 ) :
      TEXT  = TEXT                [ LM + 1 :                                 ]
  ############################################################################
  BODY      = TEXT . decode       ( "utf-8"                                  )
  if                              ( len ( BODY ) <= 0                      ) :
    return                        [                                          ]
  ############################################################################
  MDIR      = os . path . dirname ( os . path . realpath ( M3U )             )
  MDIR      = MDIR . replace      ( "\\" , "/"                               )
  ############################################################################
  LISTs     = BODY . split        ( "\n"                                     )
  FILMs     =                     [                                          ]
  ############################################################################
  for F in LISTs                                                             :
    ##########################################################################
    K       = F
    K       = K . replace         ( "\r" , ""                                )
    K       = K . replace         ( "\n" , ""                                )
    ##########################################################################
    if                            ( len ( K ) <= 0                         ) :
      continue
    ##########################################################################
    if                            ( "#" == K [ 0 : 1 ]                     ) :
      continue
    ##########################################################################
    if                            ( ( "/" in F ) or ( "\\" in F )          ) :
      ########################################################################
      T     = F
      ########################################################################
    else                                                                     :
      ########################################################################
      T     = f"{MDIR}/{K}"
    ##########################################################################
    if                           ( os . path . exists ( T )                ) :
      ########################################################################
      FILMs . append             ( T                                         )
  ############################################################################
  return FILMs
